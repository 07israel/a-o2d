		       <h1 id="p-1">Super Nintendo Ganhará Um Novo Jogo</h1><br>
			   <h3> Fork Parker's Crunch Out</h3>
			   <h5>Todos os lucros irão para a caridade da saúde mental</h5>
				    <p>
					 
				<p>A gravadora Indie Devolver Digital e a desenvolvedora Megacat Studios fizeram uma parceria por uma boa causa e lançaram o clássico jogo de estilo arcade Fork Parker Crunch Out… no SNES.<br></p>
				 <br>		
				<figure>
				      <img class="img-fluid"  width="550"  alt="" src="_img/games/jogo-novo/snes.png">
				</figure>
				 <br>		
              <p>Com um preço de US $ 49,99, o título leva o nome do famoso CFO imaginário do Devolver, com a editora doando todos 
			  os lucros de vendas para a instituição de caridade Take This, que fornece informações. educação e serviços dentro da indústria de jogos para lidar com questões de saúde mental.		
		</P>
		
             <p>m Crunch Out, você joga como Fork Parker enquanto ele tenta motivar uma equipe de desenvolvedores. “Elementos sem sentido como 'integridade artística' e 'jogabilidade envolvente' estão fora da 
			 janela, já que a eficiência e os prazos agora ditam todos os movimentos do estúdio - bem-vindos ao tempo”, descreveu a Megacat em seu site. 
		  </p>
		   	<figure>
				      <img class="img-fluid"  width="550"  alt="" src="_img/games/jogo-novo/fork-p.jpg">
				</figure>
		     <p>
			 Você pode comprar o Crunch Out do Fork Parker no site da Megacat . O cartucho autêntico (que vem com um manual clássico) é limitado a 1.000 cópias.
			 
			 </p>
			 
			 <p>
			 Kate Edwards, membro do conselho da Take This, comentou: "Como demonstrado em nosso livro branco de 2016, Crunch Hurts, o crunch é uma prática insustentável que explora a paixão dos criadores em detrimento 
			 de seu bem-estar físico e mental. Aplaudimos e valorizamos o Devolver. A abordagem criativa da Digital e Megacat Studios para expor a realidade da crise enquanto generosamente apoia a nossa missão. "
			 </p>
			 
			 <p>O próprio Parker Fork acrescentou: “Quando fui abordado pela primeira vez sobre um jogo de caridade, presumi que se tratava de um esquema inteligente de proteção fiscal e era tudo por ele. Depois de ter sido explicado várias vezes, finalmente, que isso era para realmente ajudar as pessoas boas de Take This, eu concordava relutantemente. ”</p>
			 
			 
