		       <h1 id="p-1"> Super Mario Odyssey </h1><br>
			      <h5>Características principais</h5>
				    <p>
				  A Nintendo sempre foi o melhor do negócio na criação de espaços criativos e criativos de mundo aberto e sandbox. 
				  Colocando sua mascote moustachioed ao leme de seus maiores títulos, cada nova entrada traz consigo as maiores expectativas. 
				  Então, como competir com si mesmo? Se você é a Nintendo, você criou uma carta de amor para tudo o que veio antes, e é exatamente 
				  isso que é feito com o Super Mario Odyssey, potencialmente o maior da série até o momento e um verdadeiro sucessor do Galaxy .<br>

				   Já faz um tempo que Mario correu selvagem em uma caixa de areia, e Odyssey consegue capturar o que fez do Super Mario 64 um tesouro: 
				   o amor por cada Reino. Enquanto o 3D World e as várias ofertas de 2D tiveram níveis divertidos, mas isolados, a decisão de retornar a 
				   um campo de jogos extenso prova magistral, pois mais uma vez permite que as personalidades dos mundos brilhem. E cada um pode rivalizar 
				   com os que vimos todo o caminho de volta em 1997.<br>
				   
				  <figure>
				    <img class="img-fluid"  alt="super-mario-odyssey" width="500" height="350"   src="_img/page-2/_cabecalho/mario1.jpg">
				  </figure>
				  <br>
				   
				   Ao permitir aos jogadores a liberdade de explorar mundos inteiros para coletar dezenas de Power Moons - seriamente, pode haver mais de 70
				   para encontrar em cada um dos Reinos - aprender cada centímetro do terreno se torna uma alegria. Quando criança, eu sabia o que estava dentro 
				   de cada pintura em 64, até o último detalhe, e correr por Nova Donk City, Fossil Falls e todas as outras terras em Odyssey é tão viciante. 
				   Os mundos são simplesmente deslumbrantes, cada um repleto de personalidade única.

                   Com tanto para ver e fazer, aprender a explorar tudo é uma das maiores forças de Odyssey, e isso ajuda que o Mario controle melhor do que nunca, 
				   para permitir que você o faça.

                   Como você já pensou, Mario juntou-se com Cappy, que se torna seu chapéu e oferece uma série de habilidades novas. 
				   Estes tornam-se a base para ataques e saltos, e podem até ser usados ​​para assumir o controle de personagens icônicos dentro do jogo. 
				   Por exemplo, lançar Cappy à frente de Mario antes de saltar sobre ele irá fornecer um flip de frente maior, ao jogá-lo enquanto estiver no 
				   ar concederá um segundo adicional de tempo de ar para fazer esse enorme salto. Mas além dessas vantagens, Mario simplesmente "sente" melhor.
                  <br>
                   <figure>
				    <img  class="img-fluid"  alt="super-mario-odyssey" width="500" height="350"   src="_img/page-2/_cabecalho/mario2.jpg">
				  </figure>
					
					De alguma forma, os controles se sentem mais apertados: Mario parece mais responsivo, e seus saltos mais afiados. Acople isso com um arsenal expandido de saltos e ataques, e dentro de algumas horas do tempo de jogo você se sentirá como um mestre absoluto. Havia um ponto em que, para chegar a um precipício isolado, fiz um salto de comprimento, joguei Cappy, depois fiz um mergulho (um novo movimento para Mario, onde você inicia uma libra do solo antes de atacar 'ataque', então ele pula para a frente) Então, rejeitou Cappy para pousar na beira do penhasco. Ninguém estava por perto para o alto de cinco eu era a única parte triste sobre essa conquista monumental.
                   <br>
                    Novamente, bem como as novas capacidades de Mario, também há muito para ver e fazer em todos os mundos. 
					A criatividade e a inovação da Nintendo estão em absoluto aqui, com tantas maneiras divertidas de encontrar e desbloquear Power Moons. 
					Ao contrário de 64, Galaxy e Sunshine (além das lendas baseadas em histórias principais), você deve encontrar o Power Moons sozinho. 
					Não há sugestões - basta sair e explorar o mundo. No começo, fui adiada por isso, preferindo a abordagem de 64 de ter uma diretiva muito 
					clara para obter uma Estrela, mas rapidamente cresceu em mim. Ser capaz de coletar dezenas de Power Moons em uma única saída, com um olho 
					interessado ajudando a encontrar mais, logo me apaixonei por explorar, interagir e jogar Cappy em todas as direções. Encontrando uma Lua 
					do Poder simplesmente porque eu ousei aventurar-me fora do caminho mais conhecido, ou trabalhando para alcançar o ponto mais alto de um 
					mapa com saltos e jogadas perfeitas em polegadas, sentiu-se tão bom. Incentiva a experimentação e recompensa-o por isso.

                   <figure>
				    <img class="img-fluid" alt="super-mario-odyssey" width="500" height="350"   src="_img/page-2/_cabecalho/mario3.jpg">
				  </figure>
					
					Sobre o assunto da experimentação, agora você pode arrumar o Mario em inúmeras roupas diferentes. Alguns são necessários para alcançar determinadas Power Moons, enquanto outros são apenas divertidos para vestir Mario. Existe uma loja 'Crazy Cap' que aceita duas moedas: moedas de ouro e moedas roxas. Os roxos podem ser coletados em cada reino, mas só podem ser gastos no local onde são encontrados, enquanto os ouro podem ser coletados e gastos em qualquer lugar.
                       <br>
                    Em uma era de microtransações, é refrescante ver a Nintendo completamente evitá-los e simplesmente fazer isso uma coisa divertida para os jogadores aproveitarem, e por isso rapidamente me assustei com isso. Eu queria colecionar todas as roupas que pude. É um ótimo sistema que não oferece nenhuma recompensa tangível além de ver os diferentes looks de Mario, mas a diversão de colecionar as moedas em cada reino faz com que valha a pena.
                    <br>
					 É estranho discutir spoilers em relação a um jogo de Mario, mas o final de Odyssey (sem entrar em detalhes) é o maior final para qualquer videogame que já joguei. Não muito diferente do Halo 3, seu rival mais próximo, o nível de brilho no show ao longo do clímax me fez sorrir com glee, tocando em todos os ossos nostálgicos do meu corpo e reunindo todas as ferramentas mostradas ao longo da jornada. É simplesmente fascinante.

					   <figure>
				    <img class="img-fluid"  alt="super-mario-odyssey" width="500" height="350"   src="_img/page-2/_cabecalho/mario4.jpg">
				  </figure>
                     
					 Além disso, como os melhores jogos de Mario, o final é onde a diversão realmente começa. Muito mais do jogo é desbloqueado após o lançamento dos créditos. Além de voltar a colecionar as centenas de Power Moons que você pode ter perdido, também há muitas novidades para descobrir. A Nintendo realmente não reteve nada neste jogo.

                         <br>
                     A única lodo que eu posso fazer na Odyssey é que, bastante bizarra, alguns movimentos de Mario estão bloqueados para quem não está jogando usando controles de movimento. Para aqueles que usam o Pro Controller ou que tomam o Nintendo Switch em movimento, um monte de movimentos no arsenal de Cappie será fora de limites, o que é estranho, considerando que é o USP inteiro do console.

                     <h5 id="p-2">VEREDITO</h5>

                     <p>Ele carregou expectativas geniais, mas de alguma forma a Nintendo conseguiu saltar sobre a barra por uma enorme distância. Oferecendo a experiência de jogo mais satisfatória de qualquer jogo de Mario até à data, o Super Mario Odyssey é quase perfeito.</p>
                     <br>
                     <p>Alguns vão debater se ele combina ou não com o Galaxy, mas para mim, graças à sua brilhante reinvenção da fórmula sandbox e ao uso incrível de tantas ferramentas diferentes.</p>

                         <figure>
				    <img class="img-fluid"  alt="super-mario-odyssey" width="600" height="350"   src="_img/page-2/_cabecalho/mario5.jpg">
				  </figure>
                     
                     
					 <p>Você ficará obcecado com a coleta de roupas e Power Moons, e se apaixonar por todo reino que você visitar. A Nintendo teve um maravilhoso 2017 e está completando o estilo com o melhor jogo de Mario que você já jogou.




					


               
