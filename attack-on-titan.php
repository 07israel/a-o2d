		       <h1 id="p-1">Attack On Titan </h1><br>
				    <p>
				shingeki no kyojin ou attack on titan é um dos melhores animes em que pude assistir em 2017.<br>
				      Sua história é incrível, os personagens são todos bem trabalhados tanto no desenho quanto na história, tem efeitos impressionantes e isso nos faz ter muito mais interesse em saber sobre a história e querer ver o decorrer da trama.<br> 
                 Na sua primeira temporada a história se base em titãs monstruosos que perseguem a humanidade para come-los e satisfazer suas vontades.<br>
				 <figure>
				      <img  class="img-fluid  w-100 " src="_img/page-2/_cabecalho/attack-on-titan/img2.jpg" alt=""> 
				 </figure>
				 <p>A humanidade se esconde atrás 3 muralhas (Sina, Rose, e Maria) e já fazia 100 anos que não havia ataques dos titãs, porém bem no 1 ep dessa temporada aparece um titã colossal e abre um buraco na muralha com apenas um chute e da passagem para os titãs menores invadirem a muralha.<br>
				 </p>
