				 <p>E desde de o primeiro episódio tem presença de muita ação e revelações importantíssimas para o desenrolar da trama. <br><br>
				 
                 Na segunda temporada, a história começa a se desenvolver mais, algumas dúvidas que ficaram na primeira temporada passam a ser explicadas nessa. Porém surgem outras dúvidas devido ao mistério de alguns personagens.<br>
				 Umas das revelações sem dar muito spoiler, é que os titãs são originados dos humanos e que na verdade alguns possuem poderes e consciência do que está fazendo e os que não possuem consciência são manipulados por alguém para estarem exatamente onde estão.<br>
                 Enfim, a história é muito boa e interessante, ainda tem muita revelação para acontecer, quem acompanha o mangá já consegue ter uma ideia mais avançada sobre os personagens pois já ouve muita revelação.<br>
				 Porém só de imaginar como seria essa cena representada no anime já deixa aquele gostinho de quero mais.<br>
