					<h5>História</h5>
					
					<p>
					O ano é 1830. Num antigo castelo da Áustria, os adoradores do caos estavam tentando reviver o Lorde das Trevas, o Conde Dracula. O Vampire Hunter Morris Baldwin e seus aprendizes, Nathan Graves e Hugh Baldwin, tomaram conhecimento dos fatos e partiram viagem à Castlevania para impedir que o Conde Dracula ressuscitasse e usasse seus poderes maléficos.<p>Contudo, eles chegaram alguns segundos após Camilla, uma serva do Conde Dracula, ter já começado o ritual para reviver seu mestre.<br>

Para o Dracula conseguir recuperar todo o seu poder por completo, ele teria que esperar pelo completamento do ritual da lua cheia.</p> <p> Antes de o grupo conseguir lutar contra o Dracula e selá-lo novamente, o Dracula destrói o chão abaixo de Nathan e Hugh, fazendo com que eles caiam num longo abismo e deixando Morris sozinho com Dracula e Camilla. Felizmente, Nathan e Hugh sobrevivem da queda. Hugh sai correndo para salvar seu pai após dizer a Nathan que saísse do castelo. Nathan decide não fazê-lo, já que ele queria resgatar o seu mentor acima de tudo.</p> <p> Ele então explora o largo castelo, lutando contra os servos do Dracula pelo caminho.

Ao longo do progresso de Nathan pelo castelo, ele encontra-se com Hugh múltiplas vezes, que aparentava estar mudando significantemente em conceito de comportamento, vendo Nathan como um "porco que almeja glória" e que não era digno da Hunter Whip.</p> <p> Eventualmente, Nathan encontra-se com Camilla, que explica que ela e o Dracula vêm mexendo com a cabeça de Hugh para causar tais mudanças. Esforçadamente, Nathan consegue derrotar Camilla na sua forma verdadeira e, mais tarde, encontra-se com Hugh novamente. Logo após vê-lo, Hugh imediatamente ataca Nathan, dizendo que ele iria derrotá-lo para provar sua força a seu pai. Contudo, Nathan consegue derrotar Hugh e tirá-lo do controle mental.</p>

<p>Nathan eventualmente chega à sala cerimonial e tenta aniquilar o Dracula e salvar seu mestre de ser sacrificado como parte do ritual das trevas. Enquanto que ele lutava com o Dracula, Hugh consegue libertar Morris e ambos escapam do castelo, deixando Nathan para lutar sozinho contra o Conde.</p><p> Dracula primeiramente o ataca de maneira tradicional, usando de bolas de fogo, morcegos e teletransportando-se pelo local. Depois, o Dracula teletransporta Nathan e ele mesmo a uma dimensão alternativa onde ele podia assumir sua forma verdadeira. Nathan luta contra o Dracula e o derrota novamente. Antes que ele fosse selado,</p> Dracula diz:

É perda de tempo. Eu nunca morrerei completamente. Enquanto que houver trevas e desejos na alma do homem, eu continuarei a ser ressuscitado!

<p>Nathan, então, responde que sempre estará por perto para pará-lo. Dracula morre e o castelo vira ruínas. Após escapar do castelo, Nathan continua seu treinamento com Morris e Hugh
					
