					<h5>História</h5>
					
					<p>
					A história começa no ano de 2035 quando Soma Cruz está visitando o Japão como um estudante transferido e vivendo perto do santuário Hakuba (na versão japonesa, Soma é um nativo japonês, e não um estudante de intercâmbio).<br>
					Durante um eclipse solar, ele visita o santuário Hakuba com sua amiga de infância, Mina Hakuba. <br>
					Ele e Mina são então jogados dentro do eclipse, caindo dentro de um misterioso castelo onde eles encontram Genya Arikado. Arikado revela que aquele era o castelo do Conde Dracula.<br>
					Depois de um grupo de monstros aparecer e todos serem mortos por Arikado, a alma de um deles é absorvida por Soma. Arikado descreve aquilo como o "poder de domínio", poder pertencente a Soma que, naquele momento, estava "despertando".<br>
					Arikado então diz a Soma para entrar no castelo e procurar pela sala do trono, que assim entenderá o que ele quis dizer.
					
					</p>
					
					<p>
					Ao longo do progresso de Soma no castelo, ele se encontra com vários personagens diferentes, cada um apresentando um papel na profecia relacionada aos poderes do Dracula.<br>
					Graham Jones, um missionário que foi ao castelo, torna-se amigo de Soma. Graham explica a ele sobre a natureza do local e diz que o Dracula, anteriormente conhecido como imortal, havia realmente sido morto, e que seus poderes seriam transferidos para sua reencarnação.<br>
					Quando Soma vai mais além no castelo, ele se encontra com a bruxa Yoko Belnades, que lá está presente sob ordens da Igreja Católica.<br>
					Yoko está à procura de Graham pois acredita que ele seja perigoso e o herdeiro dos poderes do Conde.<br>
					Ela esclarece a natureza dos poderes de Soma, revelando que eles não eram necessariamente maléficos e sim que eram pertencentes a ele próprio. <br>
					Mais tarde, Soma conhece Hammer, um soldado do Exército dos Estados Unidos ordenado a ir ao santuário Hakuba.<br>
					Contudo, ele desistiu de sua missão para vender itens úteis. Assim, ele se torna o vendedor para Soma, vendendo várias armas, itens e equipamentos para ajudá-lo em sua aventura, tornando-se a "loja virtual" do jogo.<br>
					Um homem misterioso então aborda Soma, perguntando sobre a natureza do poder negro dele. Durante a conversa deles, o homem lhe revela que sofria de amnésia e que a única coisa de que conseguia se lembrar era que o seu nome começava com "J", passando a ser chamado assim até certo ponto do jogo.
					</p>
					
					<p>
					Soma se encontra novamente com Graham e o questiona sobre as suspeitas de Yoko. Graham admite que recebeu os poderes de Dracula, e que acredita ser a sua reencarnação.<br>
					Ele então faz uma pergunta sobre os poderes de Soma, e quando Soma responde que possui o "poder de domínio", Graham entra em pânico e muda completamente sua postura.<br>
					Preocupado, Soma conta este incidente a Yoko, que recomenda que ele se junte a ela para impedir que Graham consiga adquirir os poderes do Conde.[36] Mais à frente, Soma assiste à cena de Yoko sendo apunhalada por Graham com uma faca, este então fugindo logo após. <br>
					Yoko avisa a Soma do aumento de poder de Graham, quando então Arikado chega ao local. Este, por sua vez, promete a Soma que irá cuidar da ferida e lhe diz para perseguir Graham.<br>
					No caminho, Soma encontra-se com "J" novamente, que diz que parte de sua memória voltou e que seu nome é Julius Belmont, o homem que derrotou o Dracula em 1999.<br>
					Quando ele vai embora, Julius fala que sabe de algo sobre aquela situação ocorrente, mas, contudo, não revela nada a Soma.
					
