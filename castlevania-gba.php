			  <h5>História</h5>
			  
			  <p>
			  Lydie Erlanger, uma jovem Transilvânica, foi raptada e levada para um misterioso castelo na neblina. Seu amigo, Juste Belmont (neto de Simon) e seu amigo Maxim Kischine, precisam entrar e unir forças para resgatá-la. Quando entram no castelo, Maxim começa a agir de maneira estranha.<br> <p>Sua memória e comportamentos passam a ficar estranhos e ele aos poucos se lembra que já esteve neste castelo. Maxim sente que tem alguma relação com Drácula. No fim, Maxim recorda-se que dois anos atrás foi numa jornada para encontrar e destruir os restos mortais de Drácula, como Simon Belmont havia feito antigamente, mas ao recolher os seis itens, sua memória foi totalmente apagada, e Lydie sequestrada.</p>

<p>Mais à frente, Juste encontra o leal servo de Drácula, Death, este explica que o castelo foi dividido em duas "camadas" para acomodar dois espíritos que vivem dentro do corpo de Maxim: o espírito original e um mau, originado dos restos de Drácula. Maxim confirma isso, e admite ter sido o sequestrador de Lydie, alegando estar confrontando a si mesmo.<br> Juste encontra seu amigo novamente em outra camada do castelo, onde este revela que precisou perder a memória para salvar Lydie.<br> Ele então dá a Juste seu bracelete para ajudá-lo a encontrá-la no castelo, no entanto, quando Juste encontra-a, Death a sequestra para usá-la com intuito de unir os dois castelos, destruindo o espírito original de Maxim ressuscitando assim Drácula.</p> <p>Juste derrota Death, e passa a procurar Maxim.

Ao longo do caminho, ele recolhe os restos de Drácula, que estão espalhados por todo o castelo. No centro do castelo, ele encontra Maxim e Lydie, esta inconsciente, mordida por Maxim possuído pelas trevas.<br> Maxim revela que destruiu seu espírito original, e irritado, Juste luta contra ele.<br> Durante a luta, Maxim observa que Juste usa a pulseira que resiste ao controle dos espíritos malignos sobre seu corpo, e o espírito do mal dentro de Maxim reage de forma inesperada e foge enfraquecido com os restos mortais de Drácula, planejando usar seu sangue para adquirir potência total, transformando-se assim em Drácula. Juste acaba por derrotá-lo, escapando do castelo juntamente com Maxim e Lydie.</p> <p>Ela recupera a consciência fora do castelo e Juste vê que a marca da mordida no pescoço de Lydie desapareceu.<br> O mal do príncipe das trevas mais uma vez foi aniquilado pelos Belmonts.
