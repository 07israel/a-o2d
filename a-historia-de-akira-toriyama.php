			       <h4 id="p-6">Agora é a vez dos Games! </h4>
				   
				   <p>Além dos mangás e animes, Toriyama participou de outros projetos super empolgantes como capas de álbuns de músicas, cartazes para grnades eventos e até para filmes incluindo comédias como “Journey to the West: Conquering the Demons” de 2013. Até ilustrações de livros sobre segurança no transito o cara fez. </p>
					   
					   <p>Nos games Toriyama também se destacou, ele foi o responsável pelo design da série de RPG “Dragon Quest” que também teve uma adaptação sem um final para a tv, e o melhor jogo de RPG da história na minha opinião o Chrono Trigger, fazendo o design dos personagens e anime para o remake para PlayStation one em 1999. Participou também do design dos games “Tobal No 1”, logo após em “Blue Dragon” para XBOX 360 e “Blue Dragon: Awakened Shadow” para Nintendo DS. </p>
