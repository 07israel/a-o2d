					   <p>Resumindo Akira Toriyama é um exemplo a ser seguido, não se limitou as suas condições, correu atrás e hoje é um ícone Pop! Considerado o primeiro mangaká a fazer sucesso fora de suas fronteiras.
Isso é tudo pessoal, é claro que não é tudo tudo... mas é o resumo da história desse cara que gostamos muito, espero que tenha gostado e obrigado pela atenção!
</p>
					   
					   
			
				 
				 
			  
