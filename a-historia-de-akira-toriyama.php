		       <h1 id="p-1"> A HISTÓRIA DE AKIRA TORIYAMA</h1><br>
			        
				    <p>
				 Akira Toriyama é um autor japonês de mangá, conhecido por ser o criador de uma das séries mais cativantes da história, Dragon Ball e outros que iremos falar mais adiante.<br>
				  
				  <p>
				    Toryama, para os mais íntimos rs, nasceu em 5 de abril de 1955, hoje ele é muito famoso, e é difícil ouvir o nome dele e não pensar em Dragon Ball. <br>
					Mas nem sempre foi assim, Toriyama nasceu em uma família de classe média e por não ter grana para comprar revistas em quadrinhos, coisa que ele gostava muito, ele resolveu 
					fazer suas próprias histórias, começando a copiar ilustrações de “101 Dálmatas”, da aclamada Disney, por ter um talento para a coisa, logo quando saiu do ensino médio ele entrou
					para uma empresa de design. Mas nem tudo foi flores para o nosso guerreiro Toriyama, a sua entrada no mercado de trabalho foi um tanto desanimadora por que ele tinha muita dificuldade
					cumprir horários e com isso seu emprego só durou dois anos.
					
					<p><b>Aposto que você se identificou com ele quando comentamos sobre sua dificuldade de cumprir horários. kkkkk </b></p>
				  
				  </p>
				  
				  <p>
				  Sem emprego, o nosso guerreiro decidiu entrar em um concurso de mangá da Shone Jump em 1978( uma revista semanal de mangás publicada pela editora Sheisha), Sem ter um bom desempenho, ele 
				  decidiu tentar mesmo sem ganhar o prêmio (que era uma bolada boa em dinheiro), mas nem tudo estava perdido para Toriyama, o editor da revista na época, Kazuhiko Torishima, viu futuro nos traços do garoto.<br>
				  O resultado disso, é que a partir daí sua carreira começaria a decolar com o mangá independente Wonder Island que teve uma sequência Wonder Island 2 e foram lançados entre 1978 e no início de 1979.
				  
				  </p><br>
				  
				  
				  
			<figure class="figure">
                <img src="_img/page-2/akira-toriyama/akira-wonder-island.jpg" width="500" height="350"   class="figure-img img-fluid rounded" alt=" Wonder Island, Wonder Island 2">
                <figcaption class="figure-caption">O primeiro capítulo foca em um piloto ex-kamikaze, Petty Officer Furusu de 2ª<br> classe, que caiu no Oceano Pacífico em 1944 durante a Segunda Guerra Mundial<br> e foi levado para uma ilha estranha chamada Wonder Island.</figcaption>
              </figure>
			  
			  <p><b>Pena que nenhuma delas caiu no gosto do público.</b></p>
			  
			     <h4 id="p-2">Desistir não é uma opção para quem quer vencer!</h4>
				 
				 <p>
				 Por mais que “Wonder Island” mostrasse o talento de Toriyama, as coisas só ficaram boas pro cara quando ele lançou um novo mangá chamado “Tomato, Girl Detective”.<br>
				 Um mangá de edição única, que foi criado a pedido do seu editor chefe Torishima, que queria uma historia com uma protagonista feminina, que se olharmos bem, da para 
				 saber de onde veio a Bulma. Mas Toriyama não estava muito confortável com a criação dessa história, o mangá contava a história de uma detetive que resolvia os problemas
				 de uma forma desastrada, e tem uma pegada meio humorística igual aos trabalhos criados por ele que nós conhecemos hoje, e é nesse mangá que ele começa a dar nomes de 
				 comida para os personagens.
				 
				 </p> <br>
				 
				 <figure class="figure">
                <img src="_img/page-2/akira-toriyama/tomato.jpg" width="400" height="350"   class="figure-img img-fluid rounded" alt="Tomato, Girl Detective.">
                <figcaption class="figure-caption"> Este mangá também marca a primeira aparição de Kenta Kuraaku, um personagem <br>recorrente no Dr. Slump, que faz algumas aparições na mídia Dragon Ball. Akai <br>Tomato também faz uma participação especial no episódio 44 de Dragon Ball, </figcaption>
              </figure>
				   
				   <p>
				   A razão para que ele desse nome de comida para seus personagens, era que ele não gostava muito da forma com a qual ele desenhava personagens femininas. Mas por ironia do destino, o mangá foi bem aceito e abriu espaço para o seu primeiro sucesso de verdade: “Dr. Slump”, 
				   que teria uma garota protagonista.
				   
				   </p>
				 
                  <figure class="figure" id="p-3">
                <img src="_img/page-2/akira-toriyama/arale.jpg" width="350" height="350   class="figure-img img-fluid rounded" alt="Kenta Kuraaku, Dr. Slump">
                <figcaption class="figure-caption">
                        Dr. Slump ) é uma série de mangá japonesa escrita e ilustrada por Akira Toriyama.<br>
						Foi serializada pela editora Shueisha na revista Weekly Shonen Jump de 1980 a 1984,<br>
						que formaram 18 volumes tankobon.</figcaption>
              </figure>
					
					<p>Esse foi o primeiro trabalho de Toriyama publicada na série da revista Shonen Jump, em 1980, e foi através dessa belezinha que o nosso herói partiu rumo ao estrelato. </p>
					
					 <p>Dr. Slump foi originalmente serializado na Weekly Shonen Jump em 1980 e foram coletados em 18 volumes tankōbon. Foi remontado em uma edição aizōban de nove volumes, em 1990. Remontado também em 1995 numa edição bunkoban, de 9 volumes. E também no formato kanzeban de 15 volumes em 2006. </p>
					
					<p>Depois de Dr. Slump terminar em 1984, os personagens retornam em alguns capítulos do mangá Dragon Ball, em que Arale e Goku se unem para derrotar o General Blue nos eventos da Red Ribbon. </p>
					<br>
                    <figure class="figure">
                <img src="_img/page-2/akira-toriyama/arale-goku-db.jpg" width="350" height="300   class="figure-img img-fluid rounded" alt="Kenta Kuraaku, Dr. Slump, dragon ball, ação 2d">
                <figcaption class="figure-caption">
                        
                       Um tempo atrás, foi revelado que um personagem conhecido<br>
					   e amado pelos fãs de Toriyama finalmente estaria fazendo<br>
					   sua primeira aparição em Dragon Ball Super desde a série<br>
					   animada de 1997 Dr. Slump: Arale-chan.</figcaption>
					   
              </figure>
			  
			     <p>O mangá foi adaptado em uma série de de anime pela Toei Animation, que foi exibida pela Fuji TV de 1981 a 1986, consistindo de 243 episódios. Um remake foi criado treze anos após o fim do mangá, consistindo de 74 episódios que foram transmitidos de 1997 a 1999. A série também gerou vários romances, jogos eletrônicos e filmes animados.  </p>
			  
			  <p> Daí já deu pra ver que o garoto realmente iria longe. E mesmo se dedicando a “Dr. Slump”, Toriyama manteve sua carreira ativa criando histórias paralelas a essa. E isso se repetiu quando começou a criar a maravilhosa historia de Dragon Ball.  </p>
			  
			  
			  <h4 id="p-4">A Melhor Parte!</h4>
			  
			  <p>O grande, o maravilhoso, o espetacular mangá Dragon Ball, incluindo é claro todas as suas sagas, até a GT que não foi muito aceito pelo público, mas isso é assunto para outro dia. </p>
			  
			  <p> O mangá Dragon Ball começou em 1984 e foi até 1995, com a animação sendo exibida na tv entre 1986 e 1996. Entre 1996 e 1997 aconteceu a exibição do Dragonbal GT que não teve uma participação direta de Toriyama  envolvido na história, e por ele não está presente nesse anime, a galera até ignora os acontecimentos do GT, não considerando parte da historia original de Dragon Ball.</p>
					
					<h4>Por que Toriyama não se envolveu no GT? </h4>
					
					<p>Bom, o próprio Akira afirma que estava cansado de trabalhar na série, ele queria fazer outras séries de animes e mangás, e fez isso, trabalhou em outros projetos, mas nenhum dele chegaram aos pés de Dr.Slump e Dragon ball, teve uma obra lançada que até chegou perto ela se chama Go! Go! Ackman, que conta a história de um garoto que na minha opinião "esse nada mais é que o Tranks Vampiro rsrs", mas na verdade é um garoto demônio que foi lançado em 1993, teve uma adaptação para um filme em 1994 e ainda rendeu mais três jogos para Super Nintendo, teve um sucesso considerável, mas nada perto do Dragon Ball. </p>
					
					<br>
					
					    <figure class="figure" id="p-5">
                <img src="_img/page-2/akira-toriyama/go-go-akiman.jpg" width="300" height="300   class="figure-img img-fluid rounded" alt="Kenta Kuraaku, Dr. Slump, dragon ball, ação 2d">
                <figcaption class="figure-caption">
					é um mangá criado por Akira Toriyama <br>(mesmo criador de Dragon Ball).
					</figcaption>
					<br>
					<p>Já agora na atualidade do século XXI Toriyama com menos de dez mangás publicados. Dragon Ball voltaria a pauta do nosso guerreiro, com os  filmes “Batalha dos Deuses” em 2013 e a “Ressurreição de Freeza” em 2015 com o design dos personagens abrindo passagem para o mangá de “Dragon Ball Super”, nesse o Toriyama está mais envolvido com a história e quem está por conta dos traços é o Toyotaro um fanático por desenhar os personagens de Dragon Ball desde sua infância.   </p>
                       
					   
