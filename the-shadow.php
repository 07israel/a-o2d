		       <h1 id="p-1">The Shadow (O Sombra)</h1><br>
			           <h5>O Jogo Cancelado do Super Nintendo</h5>
				    <p>
				     O herói foi criado a pós a primeira guerra mundial por Walter Brown Gibson. 
					 O personagem e sua historia foi projetado primeiramente para programas de rádio 
					 em 1930 e teve sua adaptação para tirinhas de jornal, série de TV, quadrinhos, livros e games.</p>
						
				
                    <p> Na década de 80 os direitos da história foram vendidos, e dessa transação nasce seu 
					único filme, o que inspirou a criação do game. Na época o estilo de games que estava em 
					alta era os “Beat´em up” ou “Briga de Rua”, e foi nessa pegada que o jogo The Shadow foi 
					feito.</p>
				
					  	    <figure class="figure">
                <img src="_img/games/the-shadow/theshadow1.jpg" width="267" height="158"    class="figure-img img-fluid rounded" alt=" The Shadow , Ação 2D ">
                <figcaption class="figure-caption">The Shadow </figcaption>
              </figure>
			  
				
					 <br>
                    <p> O interessante é que o jogo depois de 100% completo nunca foi publicado, ao menos não oficialmente, a empresa responsável por sua adaptação foi 
					a Ocean, e apesar do game ser “Bom” não trazia uma essência ou algo que o justificasse, mesmo a historia tendo uma idade considerável para se ter sucesso, 
					o personagem era desconhecido por muitos e isso pode ter sido o motivo pelo cancelamento do game.</p><br>
					 
				<p> 
				Um fato curioso é que dizem que o Batman foi inspirado nesse personagem, mas são apenas boatos, por outro lado o jogo Batman Returns é bem parecido com o The Shadow 
				só que o game do Batman foi lançado um ano antes do The Shadow.


                </p>		
                  <h3>O GAME</h3>
                  <p>
                     The Shadow ou O Sombra assim como o homem morcego não possuía super poderes, com todo seu ar misterioso apenas hipnotizava suas vítimas com seu anel desengonçado, 
					 alguns dizem que ele ficava invisível aos olhos humanos, por isso o apelido de sombra, mas o fato é que O sombra era um homem que tinha uma mira incrível e que 
					 sabia e usava de toda sua psique para derrotar seus inimigos.
  
                  </p>	

