                    <p> O detetive de polícia Ryu Suzaku (Rick Wheeler na adaptação de 4Kids) é um dos protagonistas . 
					  Ele sofre um acidente de carro quase fatal enquanto persegue um criminoso escapado chamado Zoda. 
					 Ryu quase morre, mas é reanimado no ano de 2201 pelos membros da Jornada Móvel Task Force. Jody Summer e Dr. Stewart. 
					 A Task Force tenta manter o dinheiro dos prêmios fora das mãos de pessoas desagradáveis como a Dark Million Organization 
					 administrada por Black Shadow e Deathborn. Essa organização é responsável por o inimigo Zoda antigo da Ryog, que gera criogenicamente. 
					 Agora Ryu junta-se à Task Force para ajudar a derrubar o Zoda e o resto do Dark Million. Ao perseguir seus objetivos, 
					 Ryu irá encontrar-se inesperadamente com o lendário piloto e o caçador de recompensas chamado Captain Falcon e vários outros 
					 pilotos ao longo do caminho.</p>
