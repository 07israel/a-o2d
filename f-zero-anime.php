		       <h1 id="p-1">F-Zero: GP Legend </h1><br>
			           <h5>"F-Zero: Legend of Falcon / -Falcon Legend" </h5>
				    <p>
				     Sim ele existe, F-Zero Legend of Falcon é uma série de anime de 51 episódios da Ashi Productions com base na série de videogames. 
					 Estreou no Japão em 7 de outubro de 2003, na TV Tóquio ; o episódio final foi exibido em 28 de setembro de 2004.  4Kids 
					 Entertainment licenciou a série de anime para a transmissão norte-americana. De acordo com Kombo, na América do Norte , o show foi 
					 modificado por 4Kids. Quinze episódios de GP Legend exibidos no canal FoxBox nos Estados Unidos antes do cancelamento. 
					 Agora está sendo re-exibido em Tokyo Metropolitan Television (Tokyo MX) das 7:30 às 8:00 todas as quintas-feiras no Japão. 
					  É uma reinicialização da franquia que ocorre no ano 2201. Formas de vida de toda a galáxia vem para competir no novo 
					 torneio de corrida chamado "F-Zero".</p>
					 
					 <div class="col-sm-8 c-video">
          <h2 class="">1º EPISÓDEO</h2>
