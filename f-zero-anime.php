					 <p>Também é revelado que Ryu Suzaku é "O Salvador" do Universo e está destinado a finalmente dar uma ponta à escala em favor do Falcão. 
					 Durante a batalha final, Falcon e Ryu se uniram para destruir o Reator de matéria escura da Black Shadow com seus Reactor Mights 
					 recolhidos.<br>
					 Mas antes que o Capitão Falcon festejasse com Black Shadow, Falcon diz a Ryu que o nome de "Capitão Falcon" é um título dado
					 apenas ao melhor, que "apenas aquele que pode exceder Falcon pode se tornar Falcon".<br>
					 Depois de contar isso a Ryu, Falcon prossegue para 
					 lutar contra a Sombra Negra, terminando na derrota de Black Shadow e no sacrifício do capitão Falcon.<br>
					 Pouco depois da batalha, Ryu se 
