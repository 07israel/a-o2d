                    <p> O capitão Falcon é um dos protagonistas, e está envolto em um mistério.<br>
					 Quando não está correndo, ele trabalha em um bar que 
					  possui sob o nome de Bart (Burt na adaptação 4Kids) Lemming.<br>
					 Mais tarde, revela-se que ele é Andy Summer, o irmão de Jody, 
					 que supostamente morreu no acidente que girava em torno de Zoda (sem o conhecimento do resto do Mobile Task Force para a maioria 
					 das séries).<br>
					 Ele luta constantemente contra a Black Shadow e a Dark Million Organization, e muitas vezes dá uma mão à Task Force. 
					 No final da série, "The Legend of Falcon" é contada a Ryu pelo Dr. Stewart. De acordo com a lenda, o Capitão Falcon e a Sombra 
					 Negra são encarnações de luz e escuridão, e à maneira de Yin e Yang, eles não podem se derrotar por conta própria.<br>
					 Além disso, no 
					 início do Universo, após o Big Bang, seis objetos poderosos chamados "Reactor Mights" foram criados, e aquele que controla todos eles
					 ganha poder inimaginável.</p><br>
					 
