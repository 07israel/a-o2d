		       <h1 id="p-1"> MEGA MAN ZERO</h1><br>
			       <h5>HISTORIA</h5>
				    <p> 
					Zero é acordado aproximadamente 100 anos após a Série X pela cientista Ciel, e se vê em um mundo completamente diferente.
					<br>Contando com uma tecnologia muito superior a de sua época, humanos vivem pacificamente em uma cidade-estado intitulada "Neo Arcadia", a Elysium sonhada por X.
					<br>Todavia, o custo de tal serena forma de vida é logo revelado por inúmeros reploids sendo executados em campos de concentração sob a injusta acusação de rebelião contra a sociedade humana.
					<br>O motivo de tal radicalismo era a crise de energia enfrentada no momento, encontrando na eliminação dos reploids a solução.
					<br>Zero logo se alia a um grupo de reploids fugitivos que luta por igualdade de direitos para humanos e reploids, liderados por Ciel, humana que compartilha dos mesmos ideais.
					<br>Entretanto, Zero descobre que o líder de tal sociedade é ninguém menos que um clone de seu melhor amigo X, com quem lutara lado a lado contra os seguidores de Sigma nas Maverick Wars desde que ambos foram encontrados,o clone é conhecido como Copy X.
					<br>Agora Zero terá de ajudar a Resistência a por fim a opressão e conseguir a tão almejada paz com a Neo Arcadia, ou derrotá-la completamente junto da cópia de antigo amigo, X.
				  </p>
				  <h5>Novidades </h5>
				  
				  <p>
				 A Série Zero, continua com a temática séria que se encontrava na Série X, completando definitivamente a perda da infantilidade ocorrida na Clássica.
				 <br>Trata de temas como corrupção governamental e luta pela igualdade, e possui um enredo excepcionalmente detalhado.
				 <br>Em termos de jogabilidade, apresentou novidade como a inserção de diversas novas armas e a possibilidade de passá-las de nível, a mudança para um estilo híbrido de RPG e aventura, a diversidade de objetivos das missões e de localizações para os chefes, a presença dos Cyber Elves (programas de computador que podem atuar sobre o mundo físico), e o aumento significativo na dificuldade, contudo, é uma das poucas séries que não teve opção de jogar com outros personagens em nenhum de outros jogos além da perda do sistema de armadura.	  
				  </p>
