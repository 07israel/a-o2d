		       <h1 id="p-1">MEGA MAN 11  </h1>
			   <h3>Novo Trailer Revela Mais Sobre MegaMan 11</h3>
				    <p>
				Mega Man 11 desliza para o PlayStation 4, Xbox One, Nintendo Switch e PC em 2 de outubro de 2018! Salta, dispara, acelera e domina o teu caminho através de belos cenários 
				desenhados à mão e derruba oito novíssimos Robot Masters na mais recente luta do Mega Man pela paz eterna. Com o arsenal clássico de ferramentas e habilidades do Mega Man 
				e o novíssimo sistema Double Gear na ponta dos dedos, elimine a mais recente trama do dr. Wily para dominar o mundo!<br>
				</p>
				
				    				 <div class="col-sm-8 c-video">
          <h4 class="">TRAILER MEGAMAN 11</h4>
        <iframe width="560" height="315" src="https://www.youtube.com/embed/hcMSEPkDo_8" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe>
          <br>
          
	
        </div>
		<p>
		Não é de surpreender que o mal-humorado Dr. Wily, que é um cara de bigode, esteja à altura de seus velhos truques, mas desta vez o mestre da malevolência tem uma nova tática escondida 
		em seu jaleco. Refletindo sobre seus dias mais jovens, quando estudou ao lado de Dr. Light - o criador da Mega Man, de boa índole e sempre esperançoso -, Wily relembra uma criação própria: 
		o sistema Double Gear, capaz de empurrar robôs além de suas capacidades normais. Colocando sua ideia em prática, o médico inescrupuloso rouba oito dos robôs do Dr. Light para usar em seus 
		próprios planos nefastos. A pedido do Mega Man, Dr. Light instala o protótipo do sistema Double Gear no heróico bot azul, imbuindo o nosso herói com novas habilidades em sua busca para pôr 
		fim ao plano diabólico do Dr. Wily e continuar a luta pela paz eterna.
		</p>
		
		 <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>
		
		<p>
		Em sua essência, o sistema de engrenagem dupla tem duas funções principais que capacitam Mega Man (e jogadores) com novas habilidades. O Mega Man acelera com o Speed ​​Gear apropriadamente chamado, 
		fazendo com que o mundo ao seu redor se mova muito mais lentamente em comparação. Se você precisar de um momento para se esquivar dos projéteis, dar um salto desesperado, ou simplesmente precisar 
		de uma pausa para descobrir uma situação complicada, o Speed ​​Gear está a apenas um botão de distância para lhe dar uma chance de escapar e se adaptar rapidamente a qualquer situação. É ótimo para 
		os recém-chegados que precisam de um pouco de tempo extra para descobrir padrões de inimigos e chefes, ou veteranos que podem manobrar habilmente seus níveis.
		
		</p>
		
		 <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial2.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>
				 
				 
	  <p>
        O Power Gear aumenta a potência do Mega Man além do máximo, energizando seus disparos regulares e permitindo que ele carregue seu Mega Buster para um nível ainda mais alto do que antes para disparar
		duas tomadas de carga de cada vez. Armas especiais ganhas do Robot Masters derrotado também serão ativadas de várias maneiras quando o Mega Man tiver o Power Gear ativo. Tenha cuidado como você usa essas 
		novas habilidades! Você pode ligar e desligar cada equipamento com um simples apertar de um botão, mas empurrar Mega Man até seu limite fará com que ele superaqueça, evitando que ele use o sistema de Engrenagem 
		Dupla até que ele esfrie um pouco.

      </p>	  
	   <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial3.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>
				 
				 <p>
				 Se as coisas estão parecendo desesperadas, Mega Man também pode ativar ambas as marchas ao mesmo tempo, quando sua saúde está criticamente baixa para retardar o tempo de um rastreamento e soltar todo o seu potencial em um tiro enorme, sobrecarregado de seu Mega Buster! Cuidado, no entanto - você não pode desligar essa técnica depois que ela estiver ativa, e Mega Man será severamente enfraquecido depois. Faça valer a pena!
</p>
                <p>É claro que nenhum jogo de Mega Man está completo sem um conjunto de Robot Masters para testar sua coragem, e o Mega Man 11 apresentará mais oito chefes para o nosso herói mandar para o ferro-velho. Inicialmente revelou o projeto para um desses futuros mestres do robô como parte do Mega Man 30 th fluxo de aniversário, mas ainda não revelou seu nome até agora. Prepare-se para…
				 </p>
		 <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial4.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>		 
		
        <p>
		Bloquear Homem ! Esse vaidoso robô de construção de tijolos tem um ego do tamanho de uma pirâmide e um pouco de complexo de herói para acompanhar. Obstinado e grosso como um tijolo, Block Man é capaz de formar blocos do nada, e pode usar o Power Gear para se transformar em uma forma gigantesca quando ele for levado aos seus limites. Este Robot Master sólido não é nada complicado, mas você terá que navegar em uma série de perigosas 
		pirâmides para alcançá-lo.

         </p>	
       <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial5.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>

   <br>				 
			 <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial6.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>	
    <p>
Em seguida é uma nova revelação - Fuse Man ! Espero que isso não tenha sido muito chocante para você. Este Robot Master, de pensamento rápido, reside dentro de uma usina de energia, repleta de plataformas desafiadoras e armadilhas complicadas. Nunca um para recusar uma luta, este demônio da velocidade irá cobrar-lhe com agressão e bateria se você não for cuidadoso. Chegar ao seu covil para começar é um desafio por si só, com lasers que exigirão 
raciocínio rápido para manobrar e muitas superfícies eletrificadas que lhe darão uma sacudida se você não observar onde você pisa!
    </p>
  		 
			 <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial7.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>	
    <p>
      Block Man e Fuse Man são apenas dois dos oito robôs que se tornaram mal-intencionados devido ao conserto do Dr. Wily (como você consegue um doutorado para o mal, de qualquer forma?), E cabe ao Mega Man derrubá-los e reivindicar seus poderes como seus. Os veteranos da série sabem que cada Mestre do Robô tem uma fraqueza em relação à arma de outro Mestre do Robô, e uma grande parte da diversão é descobrir quem é fraco para o quê!
   </p>	
   
     <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial8.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>	
			<p>
     New to Mega Man 11 é como partes da aparência do Mega Man mudam quando ele equipa a arma do Robot Master, dando a ele uma aparência distinta que lembra o Robot Master do qual ele adquiriu cada arma. Também é mais fácil do que nunca escolher um poder para usar graças a uma nova roda de armas que pode ser acessada com um movimento do polegar direito, permitindo que você troque de armas na hora. Ajudando o Mega Man em sua luta pela paz eterna,
	 o Roll and Auto pode criar upgrades e itens úteis para atualizar o Mega Man usando parafusos espalhados por fases e derrubados por inimigos, dando ao Mega Man ainda mais versatilidade para combate do que qualquer outro título Mega Man anterior.

      </p>	
     <figure>
				      <img  class="img-fluid  w-100 " src="_img/games/megaman11/megaman11-oficial9.jpg" alt="MEGAMAN 11, ação 2d"> 
				 </figure>	
   
<p>
  O Mega Man 11 será jogável no salão da E3 deste ano no estande da Capcom (South Hall, estande nº 2223), então se você estiver participando do programa, ative seu próprio Speed ​​Gear para ser um dos primeiros a experimentar a mais recente aventura do Blue Bomber e veja os lindos ambientes desenhados à mão e os personagens vibrantes do Mega Man 11 com os próprios olhos nesta aventura criada por uma equipe de desenvolvimento veterana da Capcom.

</p>   
      <p>
	  O Mega Man 11 também está disponível para pré-encomenda! Deslize para o site do Mega Man 11 para pré-encomendar a versão física ou digital do jogo. Garanta sua pré-encomenda para receber uma excitante trilha sonora alternativa para uso no jogo quando for lançada em 2 de outubro. (Apenas lojas qualificadas; verifique o site para obter detalhes).
	  </p>
	  
	  <p>
	  Se outubro não pode vir rápido o suficiente, você pode acompanhar as aventuras do Blue Bomber com Mega Man Legacy Collection e Mega Man Legacy Collection 2 , agora para Nintendo Switch, PS4, Xbox One e PC, oferecendo um total combinado de dez missões clássicas! Além disso, o Mega Man X Legacy Collection 1 + 2 trará oito aventuras futuristas com Mega Man X e os outros caçadores Maverick Zero e Axl para Nintendo Switch, PlayStation 4, 
	  Xbox One e PC a partir de 24 de julho.
	  </p>
	  
