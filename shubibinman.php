		<p>  O jogo no estilo beat’em-up foi o último da série “Kaizou Choujin Shubibinman” e foi lançado em uma época em que o Nintendo 64 já existia, apostando em um acessório específico que poucas pessoas compraram. Isso ajuda a explicar o motivo de o jogo ter tão pouco reconhecimento.</p>

