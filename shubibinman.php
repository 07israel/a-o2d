		       <h1 id="p-1">Kaizou Choujin Shubibinman Zero </h1>
			   <h3>Lançamento 2017 para Super Nintendo</h3>
				    <p>
				Para quem não conhece Kaizou Choujin Shubibinman Zero é na verdade o quarto game da série, feito em 1994 na verdade chegou ao mercado apenas em 1997 via Download (Satellaview), 
				Em pleno 2017, o console lançado no início da década de 1990 recebeu um novo cartucho. Trata-se de uma remasterização em versão física de um jogo lançado exclusivamente em formato 
				digital no ano de 1997.<br>
				</p>
				
				    				 <div class="col-sm-8 c-video">
          <h2 class="">Kaizou Choujin Shubibinman Zero</h2>
         <iframe width="560" height="315" src="https://www.youtube.com/embed/R5kfWgUC1Co" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe>
          <br>
          <p>Super Nintendo</p>
	
        </div>
		<p>
		O game em questão é “Kaizou Choujin Shubibinman Zero”, que muitos de nós podem jamais ter ouvido falar. Ele foi lançado apenas pelo Satellaview, um acessório do Super Famicom 
		(a versão japonesa do Super Nintendo) que dava acesso a jogos e outros conteúdos transmitidos para a plataforma por meio de satélite. Assim, a versão em cartucho do jogo só foi 
		lançada agora, 20 anos após seu lançamento original.
		</p>
		
		<p>
		A versão física de “Kaizou Choujin Shubibinman Zero” foi lançada no Japão em junho de 2017. O preço do novo jogo foi informado pelo site GameSpot: 7.000 ienes, equivalente a R$ 190.
		
		</p>
		
		<p>É possível obter o jogo original pela <a href="https://www.amazon.com/Kaizou-Choujin-Shubibinman-Super-Famicom-Version/dp/B0725H9NNR">Amazon</a> mas, não entregam no Brasil.
		
		</p>
		
		<h4>Jogabilidade</h4>
		
		<p>
		Kaizou Choujin Shubibinman Zero é um jogo de ação de rolagem lateral, onde o jogador pode fazer uso de técnicas de luta e ataques de feixe. Os jogadores assumem o controle de Raita ou Azuki. Raita luta como um boxeador, 
		usando ataques físicos, e Azuki, luta com uma espada e juntos eles podem usar ataques combinados. Possui jogo cooperativo de dois jogadores.
		
		
		</p>
		
