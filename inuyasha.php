		       <h1 id="p-1">INUYASHA </h1><br>
				    <p>
					 
				<p>Inuyasha (Sengoku Otogi Zoshi Inu Yasha em Japonês ) é um anime de aventura, romance, comédia, ação e sobrenatural.<br>
				Considero ele um dos melhores animes que já assisti.<br>
				Sua história é voltada para um hanyou (meio-youkai) chamado Inuyasha, que foi lacrado em uma árvore (na época feudal) por uma sacerdotisa chamada Kikyou, os dois se amam porém foram enganados por um Youkai maligno e acabaram lutando um contra o outro.<br></p>
				 <br>		
				<figure>
				      <img class="img-fluid"  width="550"  alt="" src="_img/page-2/_cabecalho/inuyasha/img2.jpg">
				</figure>
				 <br>		
              <p> No Japão mostra a Kagome, uma garota comum, que misteriosamente é puxada por um poço e é perseguida por um monstro e acaba entrando na época feudal.<br>
              Lá encontra com Kaede uma velha sacerdotisa, e enquanto as duas conversam o monstro reaparece querendo matar Kagome para pegar a jóia de 4 almas (shinkon no Tama em Japonês ) que está em seu corpo.<br>
			  Enquanto Kagome foge pela floresta acaba se deparando com Inuyasha preso em uma árvore por uma flecha.<br>
             A partir dessa cena que a aventura começa, acontece muitas lutas entre youkais, aparece mais personagens importantes.<br></p>
			  <br>		
			 <figure>

			    <img class="img-fluid"  width="550"  alt="" src="_img/page-2/_cabecalho/inuyasha/img3.jpg">
			 </figure>
			  <br>		
             <p>Inuyasha no decorrer da história fica cada vez mais forte. <br>
            Esse é um daqueles animes que você não se cansa de assistir, pois sua história é muito bem feita, cada episódio revela algo importante que nos faz ficar ansiosos para ver o próximo.<br> 
            Para minha felicidade e de quem gosta desse anime, ele será relançado em 2018. 
