		      <h1 id="p-1">PROTOMAN  </h1>
			   <h3>A história de Protoman</h3>
		   
		   <p>
		   Protoman model (DLN-000) é o irmão mais velho de MegaMan, foi um protótipo feito pelo mesmo criador do MegaMan, Dr. Thomas Light, o interessante é que mesmo sendo um protótipo 
		 Blues era mais avançado do que o robozinho azul. Seu nome é Blues por ser uma referência á um estilo musical, assim como MegaMan que se chama RockMan.
		   
		   </p>
		   
		
		   
		    <figure class="figure">
                <img src="_img/games/protoman/dr-light.jpg" width="116" height="156"   class="figure-img img-fluid rounded" alt="Dr Light: Criador de MegaMan e Protoman, DLN-000">
                <figcaption class="figure-caption"> Dr. Thomas Light</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp 
				
					    <figure class="figure">
                <img src="_img/games/protoman/dr-wily.jpg" width="116" height="156"   class="figure-img img-fluid rounded" alt=" Dr Wily: Série Megaman, DLN-000">
                <figcaption class="figure-caption"> Dr. Albet Wily</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp 
				
			
			  <p>
			  Blues é um robô muito parecido com os humanos, ele tem sentimentos e pode tomar decisões não programadas, além de ter uma personalidade forte. Foi construído para ser um robô de guerra, mas, 
			  Dr. Light colocou suas emoções na criação do mesmo, e projetou um jovem robô com uma aparência inofensiva e por algumas vezes até se referiu a ele como pai,
			  mostrando assim um forte sentimento pelo robô, mas como Blues foi construído com finanças militares, Dr Light colocou nele habilidades de batalha, habilidades
			  estas que foram projetadas por Dr. Wily e foi em um breve teste que Blues começou a apresentar defeitos que o colocaria fora do jogo.
			  
			  </p>
			  
			  	    <figure class="figure">
                <img src="_img/games/protoman/energia.jpg" width="911" height="357"   class="figure-img img-fluid rounded" alt=" Proto Man: Série Megaman, DLN-000">
                <figcaption class="figure-caption">Imagem extraida do Mangá Mega Man a Origem de Proto Man</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp 
			  
		   
		   
		   <p>
		  Com vários militares observando a sua apresentação, Blues estava indo muito bem até que ao começar a fase 2 do exercício, quando vários inimigos o atacavam simultaneamente 
		  houve um problema no seu gerador de energia, e ele sofreu uma sobre carga que o fez apagar após atacar todos os inimigos do teste de uma só vez. E com isso os testes foram 
		  cancelados, os militares reconheceram o quanto Blues é realmente poderoso, mas, infelizmente o problema na geração de energia mostra que ele está muito afrente do seu tempo 
		  e por consequência não existe um gerador que o abasteça de forma eficiente, e pediram para Dr Light que investisse em um robô mais simples com menos energia para abastece-lo.
		   
		   </p>
		   
		      	    <figure class="figure">
                <img src="_img/games/protoman/soldados.jpg" width="911" height="357"   class="figure-img img-fluid rounded" alt=" Dr Light E Proto Man: Série Megaman, DLN-000">
                <figcaption class="figure-caption">Imagem extraida do Mangá Mega Man a Origem de Proto Man</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp 
			   
		   
		   <p>
		  De volta ao laboratório enquanto Blues estava se recuperando da sobrecarga, Dr Light e Dr Wily discutem sobre mudanças na programação e upgrades em Blues, de um lado Dr Light 
		  querendo melhorar o sistema de energia Blues, e do outro, Dr. Wily também querendo fazer modificações para melhorar o sistema do canhão que ele mesmo projetou e que Light instalou.
		   
		   </p>
 			 
            
          <p>Dr Wily não tem licença para operar com robôs avançados e por isso Light não o deixa mexer em Blues. Enquanto eles discutem sobre isso, Blues acorda e ouve toda a confusão, e ouve 
		  Dr Light falando sobre mudar o núcleo de sua programação, é quando ele chega e pergunta sobre que historia é essa de me modificar? Dr Light responde dizendo que Blues tem um problema 
		  de energia e que isso deve ser corrigido, e aproveitando do momento, Wily fala com alto e bom som: E se você vacilar e apagar o código original dele? Dr Light manda Blues ir se recarregar, 
		  e como ele se parece muito com um humano, começou a temer essa mudança, medo de não ser mais quem ele é depois das mudanças no código.</p>
		     <figure class="figure">
                <img src="_img/games/protoman/observando.jpg" width="911" height="357"   class="figure-img img-fluid rounded" alt=" Dr Light, Proto Man e Dr Wily: Série Megaman, DLN-000">
                <figcaption class="figure-caption">Imagem extraida do Mangá Mega Man a Origem de Proto Man</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp 
		       	
			  
			  <p>
			  Mais tarde, Blues ouve Dr Light conversando com uma pessoa através do computador e se aproxima da porta, e é aí que ele ouve Dr Light falar  em reescrever o código dele do zero, e isso foi a gota 
			  d´água para ele, pois Blues tinha medo que Dr Light alterasse sua essênsia, com lagrimas nos olhos, ou óleo nos olhos Blues pegou suas coisas e fugiu de casa e se tornou um andarilho sem destino. 
			  
			  </p>
			  
			   <figure class="figure">
                <img src="_img/games/protoman/saindo.jpg" width="911" height="357"   class="figure-img img-fluid rounded" alt=" Dr Light, Proto Man: Série Megaman, DLN-000">
                <figcaption class="figure-caption">Imagem extraida do Mangá Mega Man a Origem de Proto Man</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp 
			  
			  
			
			  
			  <p>
			 Um tempo depois quando ele pensou em voltar para casa, Blues vê uma nova criação muito parecido com ele abraçado Dr Light, essa cena cortou os circuitos dele, e mais uma vez Blues segue vagando sozinho e com ciúmes de um 
			  robô que ele nem sabia quem era.

            </p>

           <p>	Dr. light achou que nunca mais veria Blues novamente, então com os erros que ele aprendeu durante a criação de Blues, acabou fazendo da base de Blues só que melhorando o sistema de energia
              nasceu o DLN-001 chamado Rockman no japão e que nós chamamos de MegaMan e Roll sua irmã .		   
			  
			  </p>
			  
			     <figure class="figure">
                <img src="_img/games/protoman/indo.jpg" width="911" height="357"   class="figure-img img-fluid rounded" alt=" Dr Light, Proto Man: Série Megaman, DLN-000">
                <figcaption class="figure-caption">Imagem extraida do Mangá Mega Man a Origem de Proto Man</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp
				
			  
			
			  
			  <p>
			  Blues resolve embarcar em um navio que leva carcaças de robôs para descarte na floresta amazônica, chegando ao cemitério de robôs, Blues estava muito fraco e com baixa energia e acaba apagando, 
			  e por ironia do destino quem o encontra são os robôs de Dr Wily, e quando ele abre os olhos a primeira coisa que vê é Wily. Até aqui Wily não conhecia MegaMan ainda, pois estava escondido na 
			  selva amazônica.
			  
			  </p>
			  
			   <figure class="figure">
                <img src="_img/games/protoman/upgrade.jpg" width="911" height="357"   class="figure-img img-fluid rounded" alt=" Dr Light, Proto Man: Série Megaman, DLN-000">
                <figcaption class="figure-caption">Imagem extraida do Mangá Mega Man a Origem de Proto Man</figcaption>
              </figure>
			    &nbsp &nbsp &nbsp
			  <p>
			  Dr Wily deu a Blues nome provisório de "BreakMan" além de várias melhorias, trocou seu núcleo de energia que era a base de energia solar por energia nuclear, o aperfeiçoou mais ainda para combate
			  dando a Breakman um escudo chamado ProtoShild para compensar a sua falta de armaduras e um capacete com óculos e máscara para que Dr Light não o reconhecesse.
			  <br> Breackman reconhece que Dr.Wily salvou sua vida trocando seu núcleo de energia, e como um favor trabalha para Wily.
			  <br>
			  Foi trabalhando no DLN-000 Breakman (Blues) que o Dr Wily aprendeu a fazer seus próprios robôs, e isso o fez ficar mais ganancioso em seus projetos de conquistar o mundo.
			  
			  </p>
			  
			  <p> 
			  Escondido com Dr Albert Wily na floresta amazônica, Breackman e Wily assistem de onde estão por uma TV tudo o que acontece no laçboratório de Dr Light
			  </p>
			  
			  <p>
			  A primeira vez que Breackman se encontrou com MegaMan foi quando MegaMan estava recebendo a chave da cidade como reconhecimento de suas ações em ajudar e proteger os humanos.
			  <br> Breackman aparece com seu asovio clássico e atira na chave que MegaMan acabara de receber. MegaMan até que queria resolver as coisa pacificamente mas... Breackman o atacou
			  sem piedade ferindo primeiro o Rush o robô cachorro que ajudava Megaman, outros robôs tentaram ajudar MegaMan atacando Breackman mas, não tiveram sucesso, Breackman era incrivelmente 
			  poderoso, Breackman continua focado em MegaMan e o ataca com sucesso, MegaMan assume a derrota diante de todos e faz um único pedido "Acabe comigo mas não destrua meu chip, não destrua o
			  coração do meu pai", ouvindo isso Breackman relembra de bons momentos com Dr Light e acaba se destraido, MegaManga se aproveita e arranca o capacete de Breackman que sai correndo do local 
			  onde se passou a batalha.
			  
			  </p>
			  
			  
