		       <h1 id="p-1"> Megaman X Detonado</h1><br>
			       <h5> Historia </h5>
				    <p>“A série se passa no ano 21XX, Dr. Cain, um famoso paleantologo participava de uma escavação em busca do fossil de uma planta, 
					quando achou por acidente o antigo laboratório de Thomas Light, e para sua surpresa encontrou um robô chamado Mega Man X em uma cápsula
					em meio os destroços do antigo laboratório de Light. Mega Man X (chamado de ‘‘X’’) possuía o poder de tomar suas próprias decisões, o 
					que surpreendeu Cain. Ele (Dr. Cain) começa um estudo sobre X, e com o tempo, conseguiu fabricar seus próprios robôs, baseados no projeto
					de X, chamados de Reploids.<br>
                    Mas, de alguma forma, muitos dos Reploids criados por Cain começaram a se rebelar atacando os humanos. Esses Reploids foram chamados 
					deMavericks. Com isto foi criado os Mavericks Hunters, um grupo formada por Reploids, que seria responsável pelo controle de eventos com 
					Mavericks.<br>
                    Sigma,um reploid de grandes capacidades, foi escolhido para ser o líder dos Mavericks Hunters. Sigma liderou com firmeza e controle da 
					situação, até quando, a partir de certo evento, começou a se comportar de forma estranha.<br>
                    Não muito tempo depois, Sigma, o líder dos Mavericks Hunters se tornou um Maverick, e além de força, Sigma possuía uma grande inteligência 
					e influencia sobre os reploids, o que lhe garantiu um grande exercito ao seu dispor. Isso faz de Sigma o principal inimigo de X na maioria 
					dos jogos da série.”
				  </p><br>
			
				  <figure>
				     <img class="img-fluid" alt="Detonado megaman x, mega man x, mega men x" width="300" height="400"   src="_img/page-2/_cabecalho/detonado-mega-man-x/inimigo.jpg">
				         
               </figure>
				    <br>
					<p>Na primeira fase o minimo que tem que ser feito é se cuidar para sofrer o minimo de danos possíveis, é uma fase muito fácil
					só prossiga e atire em tudo que ver pela frente.<br>
					Ao chegar no final vai aparecer um inimigo muito forte, ele é VILE, deixe ele te acertar por que lutar contra ele será inútil
					no momento, quando estiver 1% de vida o ZERO vai aparecer e arrancar o braço do inimigo, logo após isso ZERO e X começam um dialogo 
					e ao termino poderá anotar o passwolrd.
					  
					</p>
				 <br>
                  
				    <h5 id="p-2">SELEÇÃO DE CAMINHO </h5>
					<br>
                        <figure>
				     <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="350" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/selecao.png"> 
               </figure>
                 <br>				  
				 <br> 
				 <p>Nessa parte, tem que escolher o caminho a ser seguido, e é claro que tem que ser o caminho maís simples quando se trata de MegaMan.
				 <br>
				 Antes de escolher um caminho, é importante saber que destuindo alguns chefes poderá afetar estáges de outros chefes.<br>
				 Por exemplo: Se derrotar o Chill Penguim antes de ir na fase do Flame Mammouth, a fase dele ficará congelada, caso contrário 
				 estará tudo funcionando normalmente e irá ter lava por todos os lados.
			    </p>
				 <br>
				 <h5 id="p-3">Chill Penguim  </h5>
				     <h6>Snow Montain Stage </h6>
				  <p>
			      Para começar, primeiro selecione a fase do Chill Penguim, ela é simples e é nela que X irá pegar a parte da armadura mais indispensável
				  do jogo, Sim digo indispensável porque pode terminar o jogo faltando qualquer parte da armadura penos a armadura das pernas, vai notar
				  o que falo quando encontrá-la.
				 </p>
				           <figure>
				     <img class="img-fluid" alt="Detonado megaman x, mega man x, mega men x" width="300" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-1.jpg"> 
                         <figcaption class="figure-caption"> Poder: Shotgun Ice<br>Fraqueza: Fire Wave </figcaption>	
			  </figure>
			  <p>
			  Não deixe ele chegar no gancho que fica no teto da sala, caso contrário ele provocará uma ventania que fica
			  um pouco complicado para se movimentar contra a mesma.
			  <br>
			  Os movimentos do Chill Pequim são muito simples e logo da para pegar o jeito.<br>
			  Depois de derrotá-lo X ganha a primeira arma.
			  É a SHOT gUN iCE: ela congela os inimigos e causa grande danos ao Spark Mandrill.
			  <br>
			  Pegando a deixa da arma, o próximo chefe a ser escolhido é o Spark Mandrill da fase Power Plant Stage.
			  </p>
			      <h5 id="p-4">Spark Mandrill </h5>
				             <h6>Power Plant Stage</h6>
			  
			             <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-2.jpg"> 
                         <figcaption class="figure-caption"> Poder: Eletric Spark.<br>Fraqueza: Shotgun Ice </figcaption>	
			  </figure>
			    <p>
				  A fase dele não é muito complicada, fique esperto quando as luzes começarem a apagar porque surem inimigos muito velozes nas escuridão.<br>
				  alguns elementos podem ser pegos nessa fase, mas terá que voltar para busca-los mais tarde.
				</p>
				
				<h5 >Sub Chefe </h5>
				         <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/sub-chefe.jpg"> 
                         
			  </figure>
			  <p>
			     Um sub chefe meio complicado, a regra é simples basta se desviar dele e atacar na hora certa.
				 ele pussui dois tipos de ataques, atira ondas elétricas no chão e bolhas para prender. 
				 Além disso ele fica de um lado para o outro com a finalidade de atingi-lo, basta usar Storm Tornado 
				 (caso tenha derrotado o Storm Eagle)
			  </p>
			  
			  <p>
			    Para acabar logo com o Spark Mandrill use a Shotgun Ice para congela-lo, não deixa ele se mover
				leva um tempinho mas é fácil.<br>
				
				Após derrotálo comemore porque acaba de ganahar o poder do choque (Eletric Spark).
			  </p>
			  <h5 id="p-5">Armored Armadillo </h5>
			      <h6>Gallery Stage</h6>
				   <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="350" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-3.jpg"> 
                         <figcaption class="img-fluid"  class="figure-caption"> Poder: Rolling Shield.<br>Fraqueza: Eletric Spark </figcaption>	
			  </figure>
			  <p>
			   Fase pedreira, mas cheia de itens. Acerte os morcegos para ganhar energia. As horripilantes criaturas
			   voadoras enchem o saco.<br>
			   Primeiro item as ser pego nessa caverna é o sub tank, que está atras de um inimigo gigante que escava a
			   caverna.<br>
			   espere ele sair do lado da parede e pegue o capacete. O coração (Heart Tank), no segundo inimigo escavadeira 
			   o tempo é essencial, assim que cair e avista-lo tem que descarregar tiro nele ou usar o Fire Wave para destruir 
			   mais rápido sendo assim em frente você irá pegar o coração.
			  </p>
			  
			  <p>
			    Para derrotar o chefe use o Eletric Spark.
				<br>Cuidado o cara vem com tudo para cima do X desvie de seus ataques e mire bem.
				<br> Após derrotá-lo ganhará Rolling Shield.
			  </p>
			  <br>
			  <h5 id="p-6">Launch Octupus </h5>
			  <h6>Ocean Stage </h6>
			  
			    <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="350"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-4.jpg"> 
                         <figcaption class="figure-caption"> Poder: Homing Torpedo.<br>Fraqueza: Rolling Shield </figcaption>	
			  </figure>
		      
            <p> É uma das fases mais difíceis dentre as oito primeiras. os inimigos fortões que aparecem em sua frente 
			dão o tom.<br>
			Se um peixão o engolir, lute com todas as forças para se livrar dele.
			nessa fase X irá usar suas abilidades ao máximo.
			<br>
			Para coletar um Heart Tank, para isso terá que pegar um turbilhão e pular fora do oceano em seguida haverá 
			um submarino(barco) soltando tipos de granadas com outros inimigos nos arredores que pega o megaman e 
			solta em outro lugar.
			<br>
			Quando enfrentar as grnades serpentes, use o tornado para evitar complicações.
            <br>
			Para derrotar o chefão use o Rolling Shield.
			<br>Cuidado! ele lança peixes e bombas, não deixe ele o agrarrar.
			<br>
			Após derrotá-lo X adquire o Horming Torpedo.
            </p>	
            <br>
            <h5 id="p-7">Boomer Kuwanger </h5>
             <h6>Tower Stage </h6>
                <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="350"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-5.jpg"> 
                         <figcaption class="figure-caption"> Poder: Boomerang Cutter.<br>Fraqueza: Homing Torpedo </figcaption>	
			  </figure>		
            <p>
              Estágio supervertical, com perigos a cada segundo. Boa hora para testar as habilidades conquistadas.
			  Afinal, não é fácil ficar pulando de parede em parede. Quando estiver em apuros não exite em usar o Rolling Shield.
			  <br>
			  Perto do final Você verá um coração (Heart Tank), para pega-lo basta usar a armadura do punho e a arma de gelo
			  ,energize ao máximo para fazer o ataque especial para deslizar suba em cima e quando tiver caindo da torre pule de volta em 
			  sentido a plataforma e pegue o coração.
            </p>
            <p>Para derrotar o chefe use o Torpedo (Homing Torpedo) e não deix ele jogá-lo para cima. Rapidez é fundamental.
			<br>Ao derrot-alo X adquire o Boomerang Cutter é a sua mais nova arma.

            </p>	
            <br>
               <h5 id="p-8">Sting Chameleon</h5>
			    <h6>Forest Stage</h6>
             			 <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="350"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-6.jpg"> 
                         <figcaption class="figure-caption"> Poder: Chameleon Sting.<br>Fraqueza: Boomerang Cutter </figcaption>	
			  </figure>	
			  
			  <p>Essa fase não muito complica, na verdade é uma das mais fáceis do jogo, e é aqui que X adquire a armadura 
			  que protege seu corpo.<br>
			  
		       Ao chegar num penhasco na entrada da caverna, será o ponto onde você irá coletar os dois itens.
              Ao descer pela parede a parte debaixo tem que estar com agua (basta derrotar o Octupus) agora suba nestes blocos de pedras que estão bloqueando 
              e você irá destruir, feito isso de um dash para o outro lado e pegue o coração volte e siga por com agora.
			        <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="250" height="200"   src="_img/page-2/_cabecalho/detonado-mega-man-x/heart-tank-5.png"> 
			  </figure>	
			     Na parte superior enfrente o robô que fica pulando de uma lado para o outro, se você se aproximar ele irá ataca dando uma investida com o braço, 
				 basta atirar na cabeça de longe até destruir e assim irá aparecer a capsula da armadura.<br><br>
				     <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="250" height="200"   src="_img/page-2/_cabecalho/detonado-mega-man-x/sub-chefe-2.jpg"> 
			  </figure>	
			    Logo após destrui-lo surge a cpsula onde X ganhará sua nova parte da armadura.
				<br>
				Para destuir o chefão use o Boomerang. Fique ligado na tela para sacar onde ele irá aparecer e atire.
			  
			  </p>
			  
			   <h5 id="p-9">Storm Eagle</h5>
			    <h6>Sky Stage</h6>
             			 <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="350"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-7.jpg"> 
                         <figcaption class="figure-caption"> Poder: Storm Tornado.<br>Fraqueza: Chameleon Sting</figcaption>	
			  </figure>	
			  
			  <p>
			  Na minha humilde opinião, a trilha sonora dessa fazer é a melhor.
			  A fase do céu é ótima para descolar power-ups: energia, tanque de energia e um coração para aumentar a barra de energia.<br>
			  Atire nos tanques com a inscrição (DA): Eele podem esconder itens.
			  <br>
			  Na primeira parte suba até a plataforma onde você deve prosseguir, no entanto assim que avistar volte e de meia volta dando um 
			  Dash e pula em sentido ao começo da fase e você irá cair numa plataforma de cima onde terá o coração.
			  <figure>
				  <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/heart-tank-6.png"> 
                        
			  </figure>	
			  
			    Continue normalmente até ver uma torre com canhão assim que ver ele subir destrua o inimigo e em seguida suba até a torre e atire no vidro para entrar, 
				continue no corredor e destrua o inimigo e pegue o sub tank ou suba na plataforma na parte de trás.<br><br>
				<figure>
				  <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/tank.png"> 
                        
			  </figure>	
			  
			    Prossiga pulando nas plataformas até chegar numa área com estrutura de metal, basta continuar e ver uns tanques do outro lado e destrua para ir e pegar 
				a armadura do capacete, sendo assim basta continuar a fase até chegar no chefe.<br><br>
			  	<figure>
				  <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="300"   src="_img/page-2/_cabecalho/detonado-mega-man-x/capacete.jpg"> 
                        
			  </figure>	
			  Chefe. Quando ele começar a bater asas, corra para não ser derrubado. O lance não é
			  acertá-lo, mas sim fazer X cair.
			  Ele só ataca com vôos arasantes. Desvie e detone o cara.<br>
			  Cuidado! Um pouco antes de se render ele joga ovos cheios de pequenas águias:
			  destrua os ovos.
			  <br>Ao drrotá-lo X adquire a Storm Tornado, uma arma muito importante, como visto anteriormente, X precisará dela.
			  
			 </p>
			 
			   <h5 id="p-10">Flamme Mamouth</h5>
			    <h6>Factory Stage</h6>
             			 <figure>
				             <img class="img-fluid"  alt="Detonado megaman x, mega man x, mega men x" width="300" height="350"   src="_img/page-2/_cabecalho/detonado-mega-man-x/chefe-8.jpg"> 
                         <figcaption class="figure-caption"> Poder: Fire Wave.<br>Fraqueza: Storm Tornado</figcaption>	
