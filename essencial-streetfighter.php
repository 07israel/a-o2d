		       <h1 id="p-1"> WARPZONE Anuncia Essencial Street Fighter </h1><br>
			     
          <p>Se você é um gamer exigente e gosta de produtos de alta qualidade, certamente tem algum exemplar da
		  WARPZONE em sua estante.<br> Mas se ainda não conhece o trabalho dos caras, essa é a hora de conhecer, pois todo gamer que se prese,
		  joga ou já jogou Street Figter 2 e suas sequências, eu particularmente já apanhei por desviar a verba do frango do almoço de casa para 
		  jogar Street Fighter rs, é isso foi em meados dos anos 90, a forma usada para conseguir verba para jogar vídeo game nas casas de jogos não era bem
		  vista pelos pais da época, então quando surgia uma oportunidade tinha que ser aproveitada, mesmo que isso lhe custasse a vida.<br> 
		  No meu caso, em um certo domingo da época, minha mãe me pediu para comprar um pente para cabelo e um frango para o almoço, e eu fui, 
		  domingo em minha cidade os mercados só funcionam até o meio dia, e eu fui umas 11 horas da manhã cumprir a missão que a minha amada 
		  mãe havia me enviado, só que ao lado do super mercado havia uma casa de jogos ou fliperama ou sei lá como vocês chamavam, 
		  eu as chamava de casa de jogos. E passando em frente vi alguns amigos jogando e se divertindo com Street Fighter e eu estava com dinheiro
		  e pensei (Hoje é domingo, não tem nada de interessante na TV, eu não tenho um vídeo cassete, há... eu vou jogar um pouco e falar que perdi 
		  o dinheiro no caminho) Detalhe, o mercado ficava a uns 100 metros da minha casa.<br> Então após esperar uns 20 minutos, finalmente o vídeo 
		  game estava sem mais ninguém na fila, então eu disse, coloca 1 real de Street Fighter no capricho aí,"1 real era equivalente há uma hora de jogo", nisso eu já estava atrasado há uns 30 
		  minutos para levar o frango e o pente, e minha mãe como é muito preocupada resolveu ter a brilhante ideia de ir atrás do seu soldado que não 
		  havia retornado com o resultado da missão. Bom nesse ponto eu já deveria ter parado de falar porque as coisas ficam meio constrangedoras daqui para frente, 
		  “Cara” ela foi ao mercado e certamente o rapaz que atendia por lá, deve ter dito que eu nem tinha ido por aquelas bandas, então ela já imaginou onde eu deveria estar.<br> Bom se serve 
		  de consolo e para a história não ficar muito frustrante, ela disse mais tarde que ficou aliviada de saber que estava bem. Mas voltando de 
		  onde parei, ela foi até o vídeo game e me achou, e eu já sabia que o couro iria comer aquele dia e fiz o ultimo pedido a ela. Mãe me deixa terminar que já estou
		  ind...... ela nem me esperou terminar a frase e já me arrancou de lá e me levou para casa me escoltando cum um chicote de mato verde, e chegando em casa ela me 
		  trancou em um quarto e pegou o cinto de couro do meu pai e... Pronto dai você já deve ter uma ideia do que aconteceu, sim! Nunca mais desviei verba do frango e de mais 
		  nada para meu entretenimento ou qualquer coisa parecida. </p>
 			  
			  <p>Bom agora que já falei que para jogar Street Fighter vale apena qualquer sacrifício, eu particularmente fiquei super empolgado com o anuncio da WARPZONE de que eles vão lançar um 
			  livro licenciado pela Capcom contando tudo sobre Street Fighter, e se tratando da WARPZONE sei que o negócio vai ser top! Como tudo que eles já lançaram e inclusive as edições digitais 
			  das revistas que eles produzem e que você pode baixar totalmente de graça pelo <a href="http://warpzone.me/loja/"> site deles </a>.<br> O lançamento está previsto para agosto de 2018 segundo o <a href="https://www.catarse.me/streetfighter">Catarse</a> 
			  site que você pode apoiar o projeto, o livro está classificado como artigo de luxo e vai conter 360 páginas 120 a mais que o essencial KOF que foi outro lançamento espetacular, para maiores informações acesse o Catarse e participe do grupo da <a href="https://www.facebook.com/groups/clubewarpzone/?ref=group_header">WARPZONE</a> no face e se escreva no canal deles no <a href="https://www.youtube.com/channel/UCvUsNC0JnCUVdnyON5uZU2w">youtube</a>, todos esses canais citados aí são atualizados constantemente com novidades do projeto.</p>
           
