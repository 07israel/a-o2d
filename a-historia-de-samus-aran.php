			  <p>
			  Entretanto, uma nave dos Space Pirates liderada por Ridley atacou a colônia, logo após os Chozos saírem do planeta.<br>
			  Durante a ofensiva, Samus se depara com Ridley; nesse momento, ela pergunta se os dois poderiam ser amigos, repetindo 
			  as mesmas palavras de Old Bird, mas Ridley reponde com um ataque que atinge Samus e Virginia.<br> Enquanto isso, o pai 
			  de Samus, Rodney, se sacrificava para destruir a nave de Ridley, junto com parte de sua tripulação de piratas.
			  
			  </p>
			  
		   
		   
		   <p>
		  Samus foi a única sobrevivente do ataque a K-2L. Algum tempo após o ataque, o mesmo grupo de Chozos que visitou o planeta 
		  retorna, e encontra a garota acompanhada de Pyonchi, e decidem levá-los a Zebes, já que sozinhos não sobreviveriam.<br>
		  Entretanto, a constituição de Samus era muito fraca para que ela pudesse sobreviver em Zebes por mais que alguns dias, levando a
		  infusão do DNA de Gray Voice no corpo da criança, tornando-a adaptável ao ambiente hostil do planeta. Posteriormente, aos 14 anos 
		  de idade, Samus recebe a sua Power Suit, construída completamente com tecnologia Chozo.
		   
		   </p>
		   
		     	    <figure class="figure">
                <img src="_img/page-2/samus-aran/pyochi.jpg" width="106" height="146"   class="figure-img img-fluid rounded" alt=" Pyochi: Bichinho de Estimação">
