			  <p>
			  Depois acabou com os experimentos dos Piratas envolvendo a substância mutagênica Phazon (Metroid Prime, 2002, Game Cube), 
			  buscou soldados da Federação perdidos no Planeta Aether e salvou os nativos de invasores de uma dimensão paralela 
			  (Metroid Prime 2: Echoes, 2004, Game Cube), investigou uma energia misteriosa no sistema Alymbic, enfrentando 6 caçadores 
			  concorrentes ao mesmo tempo (Metroid Prime: Hunters, 2004, Nintendo DS), e ainda lutou contra mais Space Pirates 
			  (Metroid Prime 3: Corruption, 2007, Wii). Em seguida (Metroid II: Return of Samus, 1991, Game Boy) a Federação Galática pediu 
			  para Samus exterminar os Metroids em seu planeta natal (SR288), matando todos menos um, que foi entregue para os cientistas da 
			  Federação. Porém Ridley sequestrou-o, e Samus o seguiu até Zebes, onde começa sua nova aventura (Super Metroid, 1994, SNES) 
			  em busca do suposto último Metroid. E por fim, Samus ainda retorna ao SR388 anos depois e luta contra seu próprio clone devido 
			  a parasitas X nativos do planeta (Metroid Fusion, 2002, Game Boy Advance).
			  
			  </p>
			  
			  
