		       <h1 id="p-1"> Samus Aran</h1><br>
			     
          <p>Samus Aran é a protagonista dos jogos da série Metroid da Nintendo. 
           Ela é notável por ser uma das primeiras protagonistas de sexo feminino 
		   de um jogo eletrônico (por usar uma armadura que cobre todo o seu corpo, 
		   acreditava – se ser um homem, o que fora desmentido ao final do primeiro jogo) 
		   é uma das poucas a não ter apelo sexual. </p>
		   
		   <p>
		   É considerada por muitos uma das personagens femininas mais importantes da história dos videogames. <br>
           No início da série, acreditava-se que ela fosse apenas uma armadura, um ciborgue.<br> 
           Sua criação foi inspirada em outra personagem feminina, Ellen Ripley, protagonista da franquia de filmes Alien.
		   
		   </p>
		   
		   <p>
		   Samus é nativa da Terra, nascida na colônia K-2L. Samus vivia com seus pais Virginia Aran e Rodney Aran, até os 
		   três anos, quando a colônia recebeu a visita de dois Chozos. Os Chozos contataram os humanos esperando poder receber 
		   um pouco do Afloraltite que a colônia minerava, mas o pedido foi negado pelos habitantes. Samus, entretanto, conseguiu 
		   a atenção de Old Bird, um dos anciões Chozo. Os dois saíram em um caminhada, onde iniciam uma amizade. Samus passa a chamá-lo 
		   de "Tori Oji-chan" (literalmente, "Vovô-pássaro"), e Old Bird consegue fazer com que Pyonchi se torne o bichinho de estimação 
		   de Samus.
		   </p>
		   
