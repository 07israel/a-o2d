          <p>Com o passar do tempo, Samus concluiu missões que eram consideradas impossíveis, mas não terminou o treinamento da 
		  Galactic Federation e se tornou caçadora de recompensas.<br> 
          Sua primeira missão (Metroid, 1986, NES) era destruir as reservas de Metroids dos Space Pirates em Zebes, derrotando os 
		  líderes Kraid, Ridley e Mother Brain no processo. .</p>
		  
