                <img src="_img/page-2/samus-aran/gray-voice.jpg" width="106" height="146"   class="figure-img img-fluid rounded" alt=" Gray Voice">
                <figcaption class="figure-caption"> Gray Voice</figcaption>
              </figure>
			   
		   
		   <p>
		   Samus foi treinada por vários anos pelos Chozo, sendo que aos 14 anos já superava os limites físicos de qualquer humano comum.<br>
		   Os Chozo estavam ainda hesitantes quanto ao fato de torná-la uma guerreira até testemunharem sua compaixão por Iona Faerias que tinham 
		   perdido a razão e começado a atacar, devido à exposição a Badger Glove, uma espécie de arma biológica usada pelos Space Pirates. 
		   Old Bird diz a Samus que ela possuía sua total confiança; e assim, a caçadora deixou o planeta e foi à Federação
		   
		   </p>
 			 
               	 <figure class="figure">
                <img src="_img/page-2/samus-aran/chozo.jpg" width="380" height="282"   class="figure-img img-fluid rounded" alt="Samus Aran">
