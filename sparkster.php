		       <h1 id="p-1">Sparkster</h1><br>
			          
				    <table>
                     <tr>
					
				<td><figure class="figure">
                <img src="_img/games/sparkster/sparkster.png" width="968" height="1100"    class="figure-img img-fluid rounded" alt=" Sparkster, Ação 2D ">
                <figcaption class="figure-caption">Sparkster </figcaption>
              </figure></td>
					<td>
				    <p>Sparkster é um jogo de plataforma 2D de rolagem lateral, estrelado pelo herói homônimo 
					que apareceu anteriormente em Rocket Knight Adventures . É muito diferente do título 
					contemporâneo de Gênesis , Sparkster , pois não continua a história do primeiro jogo e 
					tem níveis completamente diferentes, inimigos e mecânica de jogo. Dessa forma, pode ser 
					considerado um spin-off da série. A introdução mostra apenas como um homem idoso captura 
					uma menina e a leva para seu dirigível. Nesse ponto Sparkster aparece e ele decide persegui-los.</p></td>
					</tr>
                  </table>					
				
                    <p> Sparkster é um roedor carregando um foguete nas costas. Ele pode se movimentar, 
					pular, pular e pendurar em galhos ou trilhos. Seu ataque regular é um golpe de espada 
					que envia um projétil circular. Através dos botões L e R, ele pode correr para a esquerda 
					e para a direita em um círculo de fogo. Manter o botão de ataque pressionado aciona um 
					medidor de foguete. Quando cheios, diferentes tipos ou ações podem ser realizadas: arrojando 
					para a esquerda, para a direita ou para cima para cobrir uma grande distância, ricocheteando 
					nas paredes em um movimento diagonal ou um ataque estacionário.</p>
			
			  
				
					 <br>
                    <p> O jogo consiste em nove níveis, dos quais os dois últimos só podem ser acessados ​​em níveis 
					de dificuldade mais altos. Sparkster coleta diamantes por pontos, derrota todos os tipos de inimigos 
					do robô e coleta maçãs para restaurar seu medidor de coração perto do topo da tela. A maior parte do 
					jogo consiste em jogabilidade de plataforma regular, com alguns desvios onde Sparkster monta um pássaro 
					robô, ou é mostrado de uma vista de cima para baixo voando no ar como um atirador de rolagem vertical. 
					Todos os níveis têm um chefe de nível, juntamente com um grande número de chefes de nível médio. O jogo 
					suporta um sistema de senha.</p><br>
					 
				

