		       <h1 id="p-1">Gekido Advance</h1><br>
			   <h3> A Vingança de Kintaro</h3>
			   
				    
					 <p>Gekido Advance: Kintaro's Revenge é um jogo de beat-up para o Game Boy Advance , criado pelo estúdio italiano NAPS Team . É uma sequela do jogo Gekido para o PlayStation.

Ao contrário da versão anterior, este jogo centra-se em um protagonista, em vez de vários personagens, como visto em Gekido. Como no jogo anterior, recebeu críticas positivas dos críticos.

Em 2018, Gekido: Kintaro's Revenge foi relançado para o Nintendo Switch </p>
				 <br>		
				         				 <div class="col-sm-8 c-video">
         
         <iframe width="500" height="500" src="https://www.youtube.com/embed/7qb12jTCddY" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe>
          <br>
          
	
        </div>
				 <br>		
              <p>Enquanto que no original havia vários personagens jogáveis, aqui podemos jogar apenas com o guerreiro Tetsuo que é enviado para resolver o mistério de uma invasão de mortos vivos num 
			  vilarejo distante. Os botões A e B dão golpe fraco e forte, respectivamente. Cada um tem um combo básico, sendo que é preciso apertar AAABBA ou AABBBA para lançar a sequência mais poderosa 
			  que termina num “Roriúgui” flamejante. Apertar B enquanto corre faz o Tetsuo dar uma voadora com outro efeito interessante: ela lança inimigos para o alto e permite sequencias infinitas no 
			  canto da tela. Não é à toa que os chefes ou vem em duplas ou não são inimigos convencionais.	
		</P>
		    
			<h3> TRAMA </h3>
             <p>O jogo se concentra em Tetsuo, que retornou à casa de seu sensei apenas para ser despachado para uma aldeia agrícola remota, onde parece que os mortos se levantam do túmulo. Após uma
			 investigação mais aprofundada, Tetsuo descobre que a aldeia tem tido estranhos acontecimentos desde que o antigo templo foi invadido por demônios. Quando ele retorna para a aldeia depois 
			 de conversar com o velho guardião do Templo, a aldeia foi atacada, supostamente, como diz um dos aldeões moribundos, por "... corvos ...". Também, parece que todos os as crianças desapareceram também.
		  </p>
