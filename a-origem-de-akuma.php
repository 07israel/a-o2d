		   <p>
		   Goutetsu ensinou várias técnicas aos seus discípulos, porém, ele proibiu alguns golpes que eram considerados perigosos, e que faziam parte do estilo 
		   Satsu no Hadou (Algo como: Onda Assassina).<br>
		   Sabendo da existência dessa técnica, Gouki totalmente obcecado pelo poder e resultados que esta poderia lhe trazer, as estudou secretamente.<br>
		   Mas essa técnica era tão poderosa que poderia fazer com que seu usuário perdesse totalmente o controle do próprio corpo e se tornasse um assassino 
		   cruel e totalmente frio sem sentimento algum. Por outro lado, o usuário aprenderia golpes muito poderosos que eram capazes de eliminar seus oponentes
		   em questão de segundos, Akuma e Hibikki eram muito amigos e descobriram onde Goutetsu escondia os documentos que contia tudo sobre o Satsu no Hadou.<br>
		   Com os pergaminhos em mãos, Gouki foi expulso do dojô por Goutetsu e começou a treinar sozinho para desenvolver e aprimorar tudo que já havia aprendido. 
		   Ele aprendeu técnicas como o Ashura Senkuu (Teletransporte), o Kujin Kyaku (Numa tradução direta: Perna Lâmina aérea), e o golpe mais famoso e mais sombrio
		   de todos, o Shun Goku Satsu (Aniquilação instantânea). Depois de dominar todos esses poderes, Gouki volta agora como Akuma (Grande demônio ou grande ogro) ao dojô e desafia Gotetsu à uma luta.<br> 
		   Durante a batalha Akuma, já dá claros sinais de dominação de uma força negra em sua alma, o deixando com a aparência de um Demonio e aplica o Shun Goku Satsu, 
		   matando assim o seu mestre, após liquidar Goutetsu, Akuma arranca o colar do pescoço do seu mestre e desde de então sempre aparece usando ele para se 
		   lembrar que venceu o seu mestre.
		   </p>
		   
