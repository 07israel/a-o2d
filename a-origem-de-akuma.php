		       <h1 id="p-1">A Origem de Akuma </h1><br>
			   
			   <p>
			   Se você é da queles que acha Akuma um vilão meia boca, tenho certeza que ao terminar de ler este texto 
			   irá mudar de ideia. O cara é como se fosse um Kratos do Street Fighter.
			   
			   </p>
			     
          <p>Akuma é um dos personagens mais sombrios de Street Fighter. 
		   Nos games, sua primeira aparição foi em Super Street Fighter II Turbo
		  no ano de 1994, como um personagem secreto e chefe oculto. </p>
		  
