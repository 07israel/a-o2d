					 <h5>2ª TEMPORADA DE CASTLEVAIA TERÁ QUATRO EPISÓDEOS A MAIS QUE A PRIMEIRA  </h5>
					 
				    <p>
					
					Segundo Warrer Ellis, roteirista da série animada Castlevania original da NETFLIX, confirmou que 
					a segunda temporada vai contar com oito episódios e todos eles estarão disponíveis já entre junho 
					e setembro de 2018.
					
					
				    </p>
					 
			<figure class="figure">
                <img src="_img/castlevania/wep.jpg" width="167" height="58"    class="figure-img img-fluid rounded" alt=" Warrer Ellis Roterista de Castlevania, Ação 2D ">
                <figcaption class="figure-caption"> <a href="https://twitter.com/warrenellis">Warrer Ellis Reterista   </a></figcaption>
              </figure>
	
					<p>O anúncio foi feito pelo Twitter em janeiro. </p>
					
					<p>
					FAQ: people keep asking, so I guess word didn't get around? We're coming back this summer, for eight episodes, from @Netflix. 
					pic.twitter.com/HckIke4REI
					
					<br>Wᴀʀʀᴇɴ Eʟʟɪs (@warrenellis) 19 de janeiro de 2018
					</p>
					
					<p>
					
					Traduzida:<br>
                    "FAQ: As pessoas têm me perguntado, então, será que a palavra não se espalhou? Nós vamos voltar neste verão, com oito episódios, 
					na Netflix".				
					</p>
					
				
                    <p> A série é baseada na história do game Castlevania 3, lançado em 1989 para o famoso Nintendinho, contando então a 
					história de Trevor Belmont, Sypha Belnades e Alucard, Que precisam enfrentar o terrível vampiro Drácula.</p>
