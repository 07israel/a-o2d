		       <h1 id="p-1"> NETFLIX FARÁ REMAKE DE OS CAVALEIROS DO ZODIACO </h1><br>
				    <p>
				  A NETFLIX está mesmo com vontade de saciar a fome dos fãs por animes de qualidade, a mais nova atração do canal será o remake da série original dos Cavaleiros do Zodíaco, isso mesmo você não entendeu errado, eu disse “remake” sabemos que o desafio não será fácil, pois se trata de uma franquia que mudou a forma a qual vemos animes, mas se tratando da NETFLIX podemos sim esperar um conteúdo de primeira, quem já assistiu Castlevania sabe do que estou falando, e para dar um tom de esperança de qualidade a nova série Knights of The Zodiac será assinada por ninguém mais ninguém menos que Eugene Son, que escreveu para séries animadas de quadrinhos como Ultimate Homem Aranha e Vingadores, a equipe parece ser de primeira, o diretor os animadores e os designers são japoneses.  A animação terá 12 episódios de 30 minutos cada, o que nos resta é esperar e confiar que tudo dará certo.
               
				  </p><br>
			
