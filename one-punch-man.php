		       <h1 id="p-1">One Punch Man </h1><br>
			          
				    <p>
				     É um anime bem divertido. O personagem principal Saitama é um super herói extremamente poderoso, que vive salvando o mundo mas não é 
					 reconhecido, ele vive intediado por não achar alguém capaz de enfrenta lo com a mesma força. No primeiro episódio já podemos ver a 
					 diferença de quando ele era um simples humano e de quando ganhou seu super poder, que aliás está gerando muita polémica de como ele realmente 
					 conseguiu os poderes.</p>
					         <figure>
				    <img class="img-fluid" alt="One punch man" width="500" height="350"   src="_img/page-2/_cabecalho/one-punch-man/one.jpg">
				  </figure>
					 
					<p> No anime ele apenas conta que foi através de muito treino, como adnominais, correndo, exercícios que qualquer pessoa pode fazer. 
					 Porém acredito que ele ja possuía esse poder oculto em seu corpo e só depois de adquirir força de vontade e determinação que conseguiu despertar essa força oculta.<br>
					 Na sua cidade é comum ter alienígenas, como não sabemos de onde Saitama veio essa teoria pode se tornar real. </p>
					 
					         <figure>
				    <img class="img-fluid" alt="One punch man" width="500" height="350"   src="_img/page-2/_cabecalho/one-punch-man/one1.jpg">
				  </figure>
					 
					 
                    <p> No anime mostra outros heróis, tem até uma academia que seleciona pessoas aptas para virarem heróis. Enfim, Saitama é um cara fodastico que com apenas um soco consegue derrotar qualquer oponente. Vale muito a pena assistir esse anime. 
                     A sua segunda temporada está prevista para 2018. Vamos ficar no aguardo de mais revelações do nosso herói fodastico. </p>
