		       <h1 id="p-1"> Sonic Forces</h1><br>
			        <h5>Análise</h5>
				    <p>
				  Sonic Forces  encontrou-se em uma posição bastante complicada no lançamento; Parte da compilação do lançamento do jogo sugeriu um fracasso em potencial. As primeiras impressões da demo e das visualizações do jogo contaram uma história de apreensão, e o fato de que a maioria dos códigos de revisão foram enviados tão incrivelmente tarde sugeriu que mesmo a SEGA se preocupasse com sua recepção. Como tal, fomos para isso com expectativas modestas, na esperança de que de alguma forma possa provar que todos estão errados. Contudo, conseguiu fazer isso? Vamos descobrir.
                 O jogo foi desenvolvido pela Sonic Team - uma divisão no SEGA que, talvez não seja surpreendente, seja responsável por criar a grande maioria 
				 dos títulos Sonic. Enquanto o estúdio criou a ocasional e impressionante aventura 3D Sonic em (semi) tempos recentes, como Sonic Colors , os 
				 fãs do borrão azul não foram impressionados por alguns dos títulos modernos da série. O side-scrolling Sonic Mania mudou as coisas no início 
				 deste ano, recebendo críticas universalmente positivas e uma base de fãs muito feliz quando lançou - mas, é claro, esse título não foi 
				 desenvolvido pelos senhores Sonic, em vez disso, foi criado por (essencialmente) fãs incrivelmente talentosos. Infelizmente, parece que a 
				 Sonic Team não aprendeu inteiramente - ou teve a oportunidade de fazê-lo - a valiosa lição oferecida aqui.<br>
				   <br>
				  <figure>
				    <img class="img-fluid" alt="sonic-forces" width="500" height="350"   src="_img/page-2/_cabecalho/sonic1.jpg">
				  </figure>
				  <br>
				   
				  Em uma frase, Sonic Forces é uma bolsa mista; Há momentos em que se sente como o escopo e a ambição para o título deve ter sido bastante 
				  grande em algum momento durante o desenvolvimento, mas, em última análise, essas ambições nunca foram plenamente realizadas para criar o jogo que poderia ter sido. Os 30 estágios do jogo são uma mistura de níveis 3D com o design Sonic moderno, os níveis de deslocamento lateral 2D com o Sonic bonito, podgy e clássico e níveis ligeiramente diferentes que fazem uso de um avatar criado pelo jogador. A mistura entre esses diferentes tipos de estágios é decente na maior parte, embora existissem aspectos de cada um dos quais não gostamos particularmente; nunca sentimos o desejo de voltar e explorá-los completamente.
                     Para explicar isso ainda mais, possamos falar sobre o enredo. Doctor Eggman está planejando assumir o planeta e Sonic é chamado para 
					 salvar o dia. Infelizmente, Sonic subestima o poder do Infinito (um novato misterioso para a equipe de Eggman) que o derrota com 
					 facilidade. Passaram-se seis meses, momento em que Knuckles e um elenco completo dos amigos de Sonic se juntam para formar a Resistência,
					 mas, sem sinal de Sonic, a equipe está desanimada. Isso é até você aparecer - sim, você. O jogador é encarregado de criar seu próprio 
					 Avatar (esta pode ser a forma de um gato, lobo, cachorro e urso entre outros) e esse personagem se junta ao time para começar a lutar 
					 contra Eggman e Infinite.
                  <br><br>
                   <figure>
				    <img  class="img-fluid" alt="sonic-forces" width="500" height="350"   src="_img/page-2/_cabecalho/sonic3.jpg">
				  </figure>
					<br>
					O sistema de criação de Avatar e o uso geral do personagem ao longo do jogo são sinais claros de que a Sonic Forces está voltada para um 
					público mais jovem. As opções para o seu Avatar são graciosamente generosas, com inúmeros itens de vestuário, como luvas, sapatos e roupas, sendo desbloqueados enquanto você progride no jogo. Ver o seu personagem ao lado de Sonic e amigos nas inúmeras cutscenes do jogo será ótimo para crianças; A maneira como a Sonic continua se referindo a você como seu "amigo" é quase desafiadora, mas é fácil imaginar que os jovens saquem dele.
                    O personagem do Avatar também joga de forma diferente para os dois Sonics - vendo que você não pode correr tão rápido quanto o hedgehog 
					famoso, você tem poderes diferentes que você pode utilizar em vez disso. Em primeiro lugar, você tem uma arma que pode ser usada 
					pressionando 'ZR', o que permite que você use fogo ou relâmpago para brisa através de inimigos e, em segundo lugar, você faz uso de um 
					mecânico de estilo gancho (que realmente se sente como deveria estar em um jogo Spider-Man ) para viajar em torno dos ambientes 3D. Também 
					há momentos em que você precisará combinar o Sonic moderno com o seu Avatar no mesmo nível, resultando em jogabilidade que troca do ritmo 
					de Sonic, para a plataforma ligeiramente mais intrincada do Avatar.
                 <br><br>
                   <figure>
				    <img class="img-fluid" alt="sonic-forces" width="500" height="350"   src="_img/page-2/_cabecalho/sonic2.jpg">
				  </figure>
					<br>
					Voltando ao nosso ponto anterior sobre ter problemas com cada um dos diferentes estilos de jogo - os ambientes 3D que hospedam o Sonic e o Avatar são geralmente decepcionados pelo fato de que quase nunca se sente particularmente no controle. A maior parte do tempo é gasto segurando seu controle para a frente ou para a direita, esperando que você não colapse em nada (não que você geralmente veja quando um obstáculo se aproxima) e tentando não voar fora da borda. Ao usar o gancho de agarrar, geralmente é apenas uma maiúscula de botão, ou reagindo às instruções na tela para voar ao redor do curso - você quer fazer todos os saltos sem qualquer sentimento de realização verdadeiramente satisfatório ou cair no seu destino de uma forma que pode se sentir completamente injusta. Isso é uma vergonha, porque há momentos em que simplesmente estourando quando Sonic pode se sentir divertido - simplesmente não
                    As seções 2D (que ainda usam imagens 3D) podem se sentir um pouco crudas às vezes também. Você sentirá muito mais controle nessas etapas, 
					e há momentos - assim como pode haver nos níveis 3D - onde tudo está funcionando muito bem, mas a mudança no método de controle e as 
					formas em que você pode atacar fazem isso as seções se sentem bastante limitadas. Sem dúvida, foi concebido desta forma para permanecer 
					fiel aos conceitos originais dos jogos Sonic 2D e 3D e, embora a mudança das coisas não tenha causado mais dúvida do que valha, 
					infelizmente não conseguiu puxar fora da mistura tão bem como poderia ter. A jogabilidade não está quebrada ou pobre, só parece 
					que há uma falta de imaginação ou instinto criativo por trás disso.
                       <br><br>
					   <figure>
				    <img class="img-fluid" alt="sonic-forces" width="500" height="350"   src="_img/page-2/_cabecalho/sonic4.jpg">
				  </figure>
                     <br>
					Os visuais do jogo consolidam ainda mais a idéia de Sonic Forces como uma experiência exagerada. A versão Switch do jogo é, 
					aparentemente, a escolha inferior quando se trata de desempenho (720p / 30fps em comparação com 1080p / 60fps na PS4, por exemplo) e a 
					falta de definição no cenário e as bordas desfocadas nos modelos de personagens são bastante evidentes, especialmente quando visualizado 
					na TV em vez da tela menor no modo Handheld. Não é ofensivo - certas áreas do jogo poderiam ter ficado absolutamente deslumbrantes com um 
					pouco de "oomph" mais gráfico - não é tão impressionante quanto você esperava. O jogo corre bem, no entanto, não percebemos nenhuma queda 
					substancial em framerate ou desempenho que afetou a jogabilidade - então provavelmente se resume a uma preferência de poder gráfico versus 
					capacidade portátil se você está se perguntando qual versão comprar.s movimentos de Mario estão bloqueados para quem não está jogando 
					usando controles de movimento. Para aqueles que usam o Pro Controller ou que tomam o Nintendo Switch em movimento, um monte de movimentos
					no arsenal de Cappie será fora de limites, o que é estranho, considerando que é o USP inteiro do console.
                      <br><br>
                     <h5 id="p-2">CONCLUSÃO</h5>

                     Para responder a nossa pergunta inicial, a Sonic Forces não conseguiu nos convencer de que é um título imprescindível da série, mas pelo
					 menos aliviou alguns dos nossos medos iniciais. Uma campanha relativamente curta de cinco horas ou mais, uma falta de dificuldade que aumenta de forma inesperada no chefe final e a mistura não coerente de seqüências 3D, seções 2D e cutscenes fazem das Forças Sonic uma experiência mista, com momentos positivos perdidos por mais fracos áreas.
                      Não é pobre como muitos temiam, e para crianças poderia ser uma experiência completamente agradável. Para a geração "hardcore" 
					  (e provavelmente um pouco mais antiga) de fãs Sonic, talvez seja melhor aceitar isso - para o melhor ou o pior - Sonic não é o que 
					  costumava ser. Fique com a Sonic Mania para o clássico Sonic divertido e deixe as forças atrair um público novo e mais novo. 
				  </p><br>
			
				 
				 
			  
