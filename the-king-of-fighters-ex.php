				  <h5>Sistema de Evolução </h5>
				  
				  <p>
				  Neste Jogo quanto mais você usa um Personagem e luta bem com ele, mais o nível dele Aumenta. Os Níveis são representados por Animais na seguinte Ordem:
<br>
Nível 0:Newcomer
<br>
Nível 1:Crow Beak
<br>
Nível 2:Black Jaguar
<br>
Nível 3:Tiger Fang
<br>
Nível 4:Thunder Hawk
<br>
Nível 5:Silver Wolf
<br>
Nível 6:Master Orochi
<br>
Nível 7:Dragon Warrior
<br>
Nos Níveis 6 e 7, a barra de especial e a de Energia mudam, ficando mais bem desenhadas.<br> Esse sistema provavelmente foi desenvolvido para o Jogador provar sua Técnica com cada personagem nas Batalhas Versus via Cabo Link (não esquecendo que é um Jogo de Game Boy Advance)
				  
