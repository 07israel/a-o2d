		       <h1 id="p-1"> THE KING OF FIGHTERS EX2 </h1><br>
			       <h5>HISTORIA</h5>
				    <p> A História fala dos Dez Tesouros sagrados, que nada mais são do que dez pesoas com poder de aprisionar ou fortalecer o Mal.
					<br>Numa História Paralela de King of Fighters, um Milionário chamado Gustav Munchausen manda a Carta Convite para o Torneio a Diversos Times.
					
					 <figure class="figure">
                             <img src="_img/page-2/kof-ex-2/Gustav Munchausen.jpg" width="203" height="248"   class="figure-img img-fluid rounded" alt="Gustav Munchausen Personagem de KOF EX2">
                                    <figcaption class="figure-caption"> Gustav Munchausen </figcaption>
                      </figure>
					
					
					<p> O Time Vitorioso do Torneio Anterior, formado por Kyo Kusanagi, uma nova personagem chamada Moe e o Irmão de Chizuru, Reiji Kagura, se surpreendem ao ver um time formado por Ninguém menos que Iori Yagami, o eterno Rival de Kyo, acompanhado de Duas Misteriosas Mulheres:</p>
					 <br>
					 <figure class="figure">
                             <img src="_img/page-2/kof-ex-2/Hero_Team_KOF_EX_2.jpg" width="245" height="194"   class="figure-img img-fluid rounded" alt="Gustav Munchausen Personagem de KOF EX2">
                                    <figcaption class="figure-caption"> Team:  Kyo Kusanagi, Moe, Reiji Kagura  </figcaption>
                      </figure> &nbsp &nbsp
					  
					   <figure class="figure">
                             <img src="_img/page-2/kof-ex-2/KOF_EX2-Yagami.jpg" width="245" height="194"   class="figure-img img-fluid rounded" alt="Gustav Munchausen Personagem de KOF EX2">
                                    <figcaption class="figure-caption"> Team: Iori Yagami, Jun, Myu   </figcaption>
                      </figure>
					
					
					<p>Jun, uma musculosa lutadora de Vale-Tudo, e Myu, uma Garota de Aparência Frágil, mas com Mortais Técnicas Ninjas que se utilizam de penas de Corvo... Os Combates seguem, até que chega a Final, onde obviamente o Time de Kyo e o time de Iori se Enfrentam.
					<br>Após uma Furiosa Luta, Kyo vence Iori e tem o direito de confrontar o patrocinador. 
					<br>Ao se encontrar com Gustav Munchausen, o patrocinador diz que não é o oponente deles e apresenta um Garoto muito Peculiar.
					<br>Seu nome é Sinobu.</p>
                       <br>
                          <figure class="figure">
                             <img src="_img/page-2/kof-ex-2/SinobuEx2.jpg" width="200" height="400"   class="figure-img img-fluid rounded" alt="Gustav Munchausen Personagem de KOF EX2">
                                    <figcaption class="figure-caption">  Sinobu </figcaption>
                      </figure>
					  
					  
                       
					<p>Kyo sente a aterrorizante Energia de Goenitz vinda do Garoto.
					<br>Gustav explica que Sinobu é uma tentativa de canalizar o poder de Goenitz, um simples escravo da mente do mesmo.
					<br>Sinobu, Furioso, diz que ninguém o controla, fica louco graças ao poder de Goenitz e ataca Kyo e seu time.
					<br>Kyo vence Sinobu com certa dificuldade. Eis que algo estranho acontece... Sinobu grita: "Saia de minha Cabeça!!!!" e depois emana uma aura Terrível.
					<br>Sinobu, com um poder devastador facilmente derrota Moe e Reiji.
					<br>Quando Kyo parecia ser o Próximo, eis que surge o time de Iori.
					<br>Myu explica que Sinobu é seu Irmão e o Décimo Tesouro Sagrado, e pediu a Iori para Salvar o Pobre Garoto.
					<br>Assim como em 96, Kyo e Iori unem suas Forças e vencem Sinobu, que cai completamente sem Forças... Gustav, ao ver a derrota de Sinobu, foge dizendo que existem milhões como Sinobu que ele podia "Aproveitar".
					<br>Iori pensa em matar o Garoto, mas Myu interfere, pois estava lá para salvar seu Irmão e não para Matá-lo.
					<br>Iori, contrariado, vai embora. Jun carrega o desacordado Sinobu e parte junto com Myu.
					<br>Reiji fala com Kyo sobre terem achado os Dez tesouros sagrados, mas Kyo está mais preocupado com a volta de Orochi, que com certeza será um Grande Problema...
                       
				  </p>
				  
