		       <h1 id="p-1"> Street Fighter Completa 30 Anos</h1><br>
			    
			
				 <p> Street Fighters 2 será relançado oficialmente para o grande Super Nintnedo, o motivo é a comemoração dos 30 anos da franquia, e quem vai ganhar com isso somos nós. A CAPCOM vai lançar uma linha de cartuchos limitados 
para comemorar as três décadas e a edição será mesmo muito especial, serão fabricados apenas 5,5 mil copias dessa belezinha, e 4,5 mil serão da cor vermelha para homenagear o grande Ryu e 1 mil serão da cor verde claro 
homenageando o brasileiro Blanka, o cartucho verde irá brilhar no escuro, será feito com um material florescente, o único detalhe é que infelizmente o comprador não vai poder escolher a cor do cartucho que irá comprar
 pois as mesmas serão distribuídas aleatoriamente, então vamos ter que contar com a sorte para achar o que queremos, mas na minha sincera opinião, não importa a cor o que importa é a nostalgia que vai nos trazer ao 
 comprar um cartucho novo.
               
				  </p><br>
			
