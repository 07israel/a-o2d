		       <h1 id="p-1"> Mega Man Legacy</h1><br>
				    <p>
				  Mega Man ou Rock Man no Japão, comemorará 30 anos de existência e a Capcom já prepara algo.<br>
				  Durante o Tokyo Game Show 2017, um painel Mega Man foi jogado no qual Kazuhiro Tsuchiya, produtor da Mega Man Legacy Collection 2 foi feito.<br>
				  De acordo com uma tradução do site Rockman Corner (via Destructoid ), o 30º aniversário deste personagem será celebrado pela Capcom.<br>
				  "Eu quero que você saiba que a Capcom não esqueceu de Rockman", disse Kazuhiro no final do evento.<br>
				  "Este (o logotipo do 30º aniversário) terá um bom uso.<br> O 30º aniversário de Rockman chegará em dezembro a dezembro, lembre-se da data!" 
               
				  </p><br>
				  
				  <h4 id="p-2">LOGO</h4><br>
				  <figure >
				     <img class="img-fluid"  width="300" height="300" alt="" src="_img/page-2/_cabecalho/logo-mega.jpg">
				  </figure>
				  <p>
				  O logotipo do Rockman / Mega Man 30º aniversário.<br> No momento, não foi mencionado o que a Capcom fará especificamente, 
				  seja uma coleção de títulos, um novo título ou apenas uma mercadoria do personagem.<br> Mega Man foi originalmente lançado 
				  em 1987 para a Nintendo NES e estava seguindo a luta de um robô contra outros seres mecânicos controlados pelo Dr. Willy. 
				  Em notícia relacionada o bombardeiro azul, é relatado que o filme está sendo desenvolvido pela 20th Century Fox desde 2015 , 
				  será dirigido por Henry Joost e Ariel Schulman , os cineastas por trás do peixe-gato.
				  </p><br>
