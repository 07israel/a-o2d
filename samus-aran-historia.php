		       <h1 id="p-1"> SAMUS ARAN </h1><br>
			       <h5>HISTORIA </h5>
				    <p> 
					Samus Aran é a protagonista dos jogos da série Metroid da Nintendo.
					<br>Ela é notável por ser uma das primeiras protagonistas de sexo feminino de um jogo eletrônico (por usar uma armadura que cobre todo o seu corpo, acreditava – se ser um homem, o que fora desmentido ao final do primeiro jogo) e uma das poucas a não ter apelo sexual.
					<br>Samus Aran é considerada por muitos uma das personagens femininas mais importantes da história dos videogames. 
					<br>No início da série, acreditava-se que ela fosse apenas uma armadura, um ciborgue. 
					<br>Sua criação foi inspirada em outra personagem feminina, Ellen Ripley, protagonista da franquia de filmes Alien.
				  
				  </p>
				  <p>
				Samus Aran foi a única sobrevivente do Planeta K-2L quando os Space Pirates arrasaram o lugar, sendo criada, desde então, pela raça alienígena Chozo e enviada a um treinamento especial, onde com quatorze anos de idade ganhou a Power Suit (uma armadura de combate feita com base no esqueleto de um Chozo).
				<br>Com o passar do tempo, Samus concluiu missões que eram consideradas impossíveis, mas não terminou o treinamento da Galactic Federation e se tornou caçadora de recompensas.
				<br>Sua primeira missão (Metroid, 1986, NES) era destruir as reservas de Metroids dos Space Pirates em Zebes, derrotando os líderes Kraid, Ridley e Mother Brain no processo.
				<br>Depois acabou com os experimentos dos Piratas envolvendo a substância mutagênica Phazon (Metroid Prime, 2002, Game Cube), buscou soldados da Federação perdidos no Planeta Aether e salvou os nativos de invasores de uma dimensão paralela (Metroid Prime 2: Echoes, 2004, Game Cube), investigou uma energia misteriosa no sistema Alymbic, enfrentando 6 caçadores concorrentes ao mesmo tempo (Metroid Prime: Hunters, 2004, Nintendo DS), e ainda lutou contra mais Space Pirates (Metroid Prime 3: Corruption, 2007, Wii). Em seguida (Metroid II: Return of Samus, 1991, Game Boy) a Federação Galática pediu para Samus exterminar os Metroids em seu planeta natal (SR288), matando todos menos um, que foi entregue para os cientistas da Federação. Porém Ridley sequestrou-o, e Samus o seguiu até Zebes, onde começa sua nova aventura (Super Metroid, 1994, SNES) em busca do suposto último Metroid. E por fim, Samus ainda retorna ao SR388 anos depois e luta contra seu próprio clone devido a parasitas X nativos do planeta (Metroid Fusion, 2002, Game Boy Advance).<br>
                  <br>Em Metroid: Zero Mission, Samus fica na forma Zero Suit Samus, e usa uma pistola a laser que pode se transformar em um chicote a laser quando necessário.
				  <br>Zero Suit Samus também aparece em Super Smash Bros. Brawl, após o Final Smash, Power Suit Samus, ou usando certos comandos nos controles do jogo.</p>
